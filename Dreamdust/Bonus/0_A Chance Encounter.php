    <h1>A Chance Encounter</h1>
    <p>
Another raid has happened. Thankfully, your carefully laid plans have helped. The lizardfolk came in much more disjointed. They made it through the traps, but it appears to have at least distracted them. This attack was not nearly as violent as the previous one. The lizardfolk only managed to carry off a few crates of supplies before your concealed rangers jumped out at them. The lizardfolk appear to have not wanted a head on fight and immediately ran.
</p>

<p>In the immediate aftermath, your rangers check the perimeter. Many traps were tripped, some intentionally. Some of the traps were stolen. Oddly, many of the signs were stolen too. When examining the traps, your rangers immediately run back to report to Pebble. One of the lizardfolk took a sleeping dart to the neck and hit a trap on the way out. It is currently groggily hanging from its ankle on a branch thanks to a snare trap.
</p>

<p>Pebble hurries over, examining the lizardfolk carefully. 
</p>

<p>The lizardfolk is short, just under four feet tall. It has a round body covered in bumpy brown and tan scales, with a stubby face and short spikes on its head. It has round black eyes that are currently blinking blearily as it swings lazily too and fro from the short tree. It is clad in armor made from tanned leather and bone, with several weapons made from stone and wood scattered on the ground under it. 
</p>

<p>Pebble quickly checks for additional weapons and absconds with them.  She secures the lizardfolk’s arms, and ties hobbles to its ankles (much easier than on a horse, with these ankles) and then carefully lowers the rope snare, ready to grab the hobbles if it tries to bolt.  
</p>

<p>The lizardfolk blinks blearily as it bumps into the ground. Its eyes slowly go into focus, before they go wide with fear. It scrambles wildly for a moment, barking and clicking in a strange language you don’t understand.
</p>
<p>(though if pebbles knows lizardfolk some of it might be recognizable.) (nope, she doesn’t)
</p>

<p>The lizardfolk looks at Pebble for a brief moment, then squints.
</p>

<p>A squirt of blood sprays out from the lizardfolk’s eye and hits Pebble directly in the face. The blood is a stick mess that burns slightly. It gets in Pebble’s eyes, and probably ruins a couple items of clothing. Pebble is blinded for several seconds, while the lizardfolk screams out in common.
</p>

<p>“No, no hurt! No kill! No burn!” 
</p>

<p>Pebble sputters, wiping her face with her sleeve.  “What the--”  when her eyes are clear, she examines the lizardfolk for injuries.  He is a little scuffed up and has a blow dart injury, but nothing serious.  “No hurt,” she repeats back at the lizardfolk.  “No hurt, no kill, no burn.”  She waits to see if that sinks in.
</p>

<p>The little lizard slows down its wild scrambling as Pebble does not take any aggressive actions. It stares at her for a few moments before answering.
</p>

<p>“... No hurt? That work?”
</p>

<p>“Yes.  No hurt.”  She very slowly holds out a hand.  “Up?”
</p>

<p>The lizardfolk gives her hand a wary glare. Without taking her hand, it slowly wiggles to a seated position.
</p>

<p>“Why?” It asks simply.
</p>

<p>Pebble tries to think of the best way to simplify her thoughts. The lizardfolk isn’t going anywhere. She finally says.  “Don’t want to hurt you.  We didn’t hurt you.  New here.  Why hurt us?”
</p>

<p>“New?” It seems puzzled, “Not stupid, don’t lie. You hurt with… point throw-thing, not kill. We take.” It seems to struggle looking for words for a moment, “Take, or die. Is desert. Is rule.”
</p>

<p>“Not only rule.  Trade, is other way.”  She considers trying to work through the concept of hospitality, but discards that for now.
</p>

<p>The lizardfolk gives her a steady look, head to toe. “You are new. I wrong. Not Coyote. Coyote never say thing like that. Hard to tell tall thinskin apart. Who?”
</p>

<p>“All of us,” she gestures at the encampment.  “Dreamdust elves.”  She points at herself.  “I’m Pebble.  Nice to meet you.”
</p>

<p>It bobs its head in a strange gesture, “I am Tik’Tak, of Machakw.” It pauses, “I thank you. No kill. Only little hurt.”
</p>

<p>“Tik’Tak,” Pebble repeats.  “If you do not attack again, we will not hurt again.”
</p>

<p>Tik’Tak holds up his tied hands, “Yes,” he says deadpan, “I very dangerous.”
</p>

<p>As Pebble reaches into the secret power of the Dreamdust elves, the little lizardfolk’s mind becomes open to her - at least the surface of it. A mix of emotions. Definitely some fear, though it is subsiding. A little embarrassment, which is definitely on the rise. Suspicion, a little bit of anger, and perhaps a small, tiny point of hope. Right now, vague memories of fire and pain at the edges of his mind. At the forefront, in big bold letters, is hunger. Not metaphorical, either. He seems like he is near starving and has been for some time.
</p>

<p>“Can’t be too careful.  I’d like to hear more about the Coyote, if they’re a threat we need to know.”  Pebble shrugs.  “But oh, I’m being inhospitable. Would you like something to drink?  Can you drink milk?”
</p>

<p>“Don’t know milk, maybe no. Water?” He cocks his head at you, “Inhos.. What? Not know. Coyotes… Coyotes thinskin, like you. Tall, like you. Skin…. Sandy skin, like you. Pointy head, like you. Wear bright skins like you, too. Ride… beast, four leg. Big, fast. Coyotes have the… shiny rock. Hate Machakw, chase us away. Burned old home, but Machakw strong. We live. Coyotes here long time, but after wolves and orangeskins fight, Coyote hurt more. Maybe not enough… enough to eat.” Tik’Tak trails off slightly.
</p>

<p>Pebble has kept her gift of thought reading up while Tik’Tak describes the coyotes, and gets a clear image of what Tik’Tak is trying to describe. They appear to be elves, clad in strange clothing and bearing some kind of polearm. In his mind, they are always riding horses that seem swift and terrifying.
</p>

<p>“Desert never give,” He concludes, “Only take.”
</p>

<p>Pebble considers that.  While she thinks, she sends a ranger to fetch water and some goat jerky.  Not the best meal in the world, but better than nothing.  The description and the brief image she’d gotten… the Coyote looked like elves.  With riding beasts, horses?  “Shiny rock?”  Something metal, maybe.  After the Lycans and Hobgoblins fought?  “The desert gives and takes.”
</p>

<p>Tik'Tak cocks his head at you sideways, "You live in desert long?'
</p>

<p>Pebble nodded.  “All my life.  And my parents’, and their parents, a long long time.  The Lycans-- the wolves?-- chased us out.”
</p>

<p>He squints, “Must be different desert, or you different. Desert never give, for us. Wolves chase. Orangeskin chase. Coyote chase. All hate Machakw. After big fight, much worse. Long time, coyotes just chase. After fight, kill us.”
</p>

<p>“Both, maybe.  Different desert, but it was harsher than this.  Why does everyone hate you?”  
</p>

<p>He shrugs, apparently a universal gesture, “Machakw take what need. Here first, before thinskins. They come, use…” He gestures at the ground, then raises up his hands, “Thing, in ground get bigger. Plant. No water enough for both, chase Machakw away so they have. Machakw take what need, coyote, wolf, orangeskin… all think we not..” He trails off for a moment before speaking again, “We here first.” 
</p>

<p>Pebble considers the small lizardfolk’s complicated answer to the question. Tik’Tak simply watches her. Before she is able to come up with a reply, the ranger returns. She has a gourd full of water and a wrapped package of jerky. She hands them to Pebble.
</p>

<p>Pebble unwraps the waxcloth and lays it flat on the ground between them. Tik’Tak’s attention immediately centers on the food, but he says nothing.  “Hospitality means that people who come here peacefully, we offer three days of water, and food if we have it to spare.  We’ve never been in places with many visitors, though, I don’t know if we can do this forever.  But for now, I share food and water with you.”  She took a single piece of jerky, to be polite, and waited to see if Tik’Tak would accept.
</p>

<p>After watching Pebble eat one of the pieces of jerky, Tik’Tak gingerly takes one of the other pieces. He sniffs it, then slowly chews on it. For someone apparently as hungry as his thoughts indicated, he eats slowly. He eats exactly two pieces of jerky, leaving almost all of it alone. He then swishes the gourd of water slightly, sniffs that too, and takes a few sips. Like the jerky, he drinks slowly and leaves most of the gourd full.
</p>

<p>He sets the gourd down and gives Pebble another curious look, “I not come in peace. I attack. Take and hurt. Why… hos-pi-tality?” he pronounces the word carefully.
</p>

<p>“You thought we were Coyote.  Made a mistake.  Sometimes… Sometimes that happens.  Hospitality means you get a second chance.  Eating together makes talking and trading easier.”
</p>

<p>Tik’Tak stares at the food for a few moments, “Will I go home?”
</p>

<p>“Yes.  We’ll ask you to carry a message for us, back to your people.  But you’ll be allowed to go home.”
</p>

<p>“Take food?” He asks, “Tik’Tak… no shaman, no chief. Just warrior. Just Tik’Tak. No listen to me, unless bring something to show you not coyotes.”
</p>

<p>“Yes, you can take it with you.”  Pebble hesitates, not sure if the lizardfolk would answer.  “How many of you are there?” 
</p>

<p>Tik’Tak gives her a careful look, “Not much as once was. Just our tribe, now.” 
</p>

<p>Pebble nods. “We’ll give you food to take with you.  Does anyone in your tribe read common?  Or other languages?  I don’t know the language you were speaking earlier.”
</p>

<p>Tik’Tak blinks, “Read? Not know word.”
</p>

<p>Pebble considers that, and writes a few words of common in the dirt with a finger.  “This.  For sending messages.”
</p>

<p>He scoots back slightly, “Send… message? Is magic? If not magic, not pretty drawing.”
</p>

<p>Pebble tilted her head, confused.  “Not magic.  Writing is putting words on paper.  Someone else can look at the paper and read the message.  Like… like stacking rocks for trail sign, but with more meanings.”
</p>

<p>“Hm,” He peers at the words, “Not something Machakw do. Sounds like magic. Also, speak Machakw. Is ours. Not many outside know.”
</p>

<p>He ponders for a few moments, “Chief Hashkeh speak thinskin… you say common? Lee’Shaa, shaman. She speak common. Teach chief, and me. Lee’Shaa speak common, better than chief, me. Smarter, too.” He blinks, “Not tell Hashkeh Tik’Tak say this.”
</p>

<p>She restrained a smile.  “I won’t.  I promise.”  She considered.  “Can we meet, in… four sunrises?  Over there,” she pointed.  “Away from our camp.  You, me, my chief and shaman, your chief and shaman.  Ask them?  I know you can’t promise, but ask?”
</p>

<p>“I ask,” He pauses, “Ask Lee’Shaa first. She nicer. Hey, you give more food, water and chief more likely to listen. Also…”
</p>

<p>He holds up his tied hands.
</p>

<p>Pebble nodded, contemplating how much food she could promise.  Probably a goat worth of jerky.  That was a lot of food, if managed right, but not so much that it would affect their own food stores.  “Let me ask a ranger to bring the food and a bag.”  She untied his ankles.  “Let’s go to the edge of camp, I’ll free your hands then.”
</p>

<p>Tik’Tak eyes the remaining jerky hungrily, but oddly doesn’t eat it or drink and of the water. He wraps the jerky up carefully and takes it with him, still having eaten only the two pieces. Pebble, knowing he is still quite hungry, can’t help giving him an odd look.
</p>

<p>“Always only eat what need. Little ones need it more.”
</p>

<p>With that, and little other explanation, Tik’Tak will happily take absolutely anything you let him have. Turns out, he has absolutely no issues carrying an entire goat’s worth of jerky on his back as well as the additional water. He’s stronger than he appears apparently. Tik’Tak will thank you, and head off into the desert.
</p>

<p>Pebble notes the direction carefully, and watches till he’s out of sight, in case the direction is a clue about where they’re living.
</p>

<p>As to what happens now… the dreamdust elves will simply have to wait and see.
