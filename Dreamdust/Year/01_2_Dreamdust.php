<p><b>Political-</b> Keeping an eye out for interlopers turns out to have been a good idea. Your watchers were expecting sneaky lizardfolk maybe, sneaking into the camp to look for valuable supplies. They were not expecting what actually happened.
</p>
<p>Late at night, your watchers were sneaking around the perimeter, hoping to find any interlopers. They heard rattling from one of the noisemaker traps set earlier. In the moonlight, one of them noticed something moving on top of one of the nearby hills where the trap had been set. With a few other sneaky elves as help, your watcher went to check it out.
</p>
<p>The interloper sighted them as they were sneaking up the hill. They had apparently been hiding under some kind of blanket or tarp, concealing their position and body shape. The figure bolted immediately upon realizing they were spotted. Your watchers report that they appeared to be an elf, dressed in some kind of cloak and wearing riding boots, judging by the tracks.
</p>
<p>Your watchers gave chase, but after a few minutes of running they hit open ground. The spy put their fingers to their mouth and made a piercingly loud whistle. Another elf wearing a poncho and a mask appeared out of nowhere, riding a horse. How they managed to do that is still baffling. The rider scooped up the spy and the two bolted. The horse was far, far too fast for your watchers to keep up with. Seriously, it was extremely fast.
</p>
<p>Still, no one was injured and your watchers examined the spy’s observation point and estimate they had only been there a couple minutes. It seems they simply hit the noisemaker trap by sheer bad luck. Whoever they were, they didn’t manage to get much if any information about you.

</p>
