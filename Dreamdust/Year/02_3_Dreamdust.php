<p><b>Event- Fire Elf Consultants-</b> The team of fire elf agricultural experts arrive early in the season, lead by a fire elf woman you haven’t met before who goes by the name the Smoke. She’s not like… wearing a mask or anything. It’s just a nickname. Her real name is Vita. Please call her Vita, actually, her team is just poking fun at her. She’s pretty nice, as it turns out.
