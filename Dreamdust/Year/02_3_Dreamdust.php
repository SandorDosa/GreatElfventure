<p>After a brief survey, the fire elf research team reports that you’re actually in a far better situation than you think you are. Your biggest advantage is the Coldwater river. It is a source of fresh, clean water that is also mountain runoff. This means not only is it a readily usable water source, it’s also highly drought resistant. They come up with a plan to use the Coldwater as a source of irrigation and water storage that should be able to enhance your ability to farm in the areas around Riverhaven.
</p>
<p>The fire elves get along well with the dreamdust elves, and end up leaving you with several quite helpful plans. In addition to the plans for irrigation and water management, they leave you with example plans of Surt’s own Aqua  Via that could be modified to deliver fresh clean water directly to Riverhaven, as well as cool houses. They also deliver some plans and formulas for alchemical purification and enrichment filters. Some of your growers believe these could be modified to support some of your more, ah, exotic crops with precise growing conditions.
</p>
<p>You have been given the Agricultural Plan temporary aspect, which will last until you Invoke it to enact one of those plans. Also remember- that invoke is free!
