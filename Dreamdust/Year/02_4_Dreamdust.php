<h2>Turn 8 Results</h2>
<p><b>Economic- Farming-</b> After a thorough review of the information the fire elf research team reviewed and referring to the vast desert survival experience that the dreamdust elves have, your farming task force comes to a conclusion: This is worth taking the time to do right. The dreamdust elves have rarely had a truly permanent settlement, and they want to fully consider the possibilities. They have never before had such a boon as the coldwater river: a water source that flows clean and will stay that way even during severe droughts. The potential impact on the dreamdust elves' very way of life is staggering.
</p>
<p>They decide to make sure that it is built right. The farms are not completed this turn because the dreamdust elves have decided to spend their time building the irrigation system to its maximum potential. They wish to take full advantage of the coldwater to create a sustainable water source that can still be used for farming even in the worst of circumstances. In this, they are successful.
</p>
<p>While the farms are not up to full scale production, the irrigation system proves a success. From now on, any permanent farms established within one square of the coldwater river will start as the Sustainable Farming Aspect. Unlike normal farms, these farms can be Invoked even during severe droughts, heat waves, or similar conditions.
</p>
<p>The Upgrade to establish farming is still available, but the one to build an irrigation system was. Things are going according to plan, it is just that the plan was adjusted to make sure that it was done right even if it took longer.
</p>
<p>From what you are hearing, the dark elves are doing a good job of handling their end of the refugee crisis so taking your time ends up seeming to be a fair plan.
</p>
<p><b>Traps-</b> Traps are successfully applied around Riverhaven! This has applied the temporary Aspect Trapped Approach. If anyone attempts to use a military skill to attack Riverhaven, these traps will automatically give you an Upgrade to your responding defensive military operations roll.
</p>
<p>These traps will last until they are triggered. Note that leaving them sitting around forever could be hazardous. If they are left sitting out, they might suddenly become dangerous to allied troop movements, scouts, or incoming traders. It’ll probably be a good idea to take them down when things eventually cool off a bit. The Aspect might also expire if there is enough severe weather or just enough time passes that they decay. Regardless, removing them does not take an action, all you have to do is declare you’re doing it.
</p>
<p><b>Political-</b> With tensions high and Coyotes being ever tricksters, it is high time for extra patrols. It ends up being a relatively quiet season in both Riverhaven and the nomadic settlement, however. With so many allied soldiers moving around and so much activity, it’d be a prime time for someone to try to slip through. However, there is no such activity discovered. Neither are there any suspicious accidents or mishaps in any of your settlements. Seems clear.
</p>
<p><b>Military-</b> The Coyotes are absolutely infuriating to fight, but fight them you do.
</p>
<p>The opening weeks of your joint deployment are marked with nigh constant harassing raids, feints, and sabotage. Coyotes come in small groups at random times, harass supply trains, fake charges, and just generally cause trouble. None of these end up being an actual attack, but it gets aggravating.
</p>
<p>Damage from this is of course limited. The Charred Eagles do a good job of keeping them away from anything actually important. The winged elf rangers also prove absolute masters of their craft as well. Even though they have well more than a hundred miles of territory to cover throughout the season, they do so with extreme prejudice. More often than not, you have warning that a Coyote harassing attack is coming and are able to react and close ranks.
</p>
<p>Frustratingly, actually pinning down the Coyotes when they’re doing one of these raids proves nearly impossible. Over the course of the raids, you get an idea of just how fast the Coyote elf horses are - with a move speed of about 80 in game terms. Even winged elf scouts can’t keep up with that nonsense, and tracking them once they manage to shake visual contact proves nearly impossible. Their habit of using small hills as cover and kicking up small dust storms makes this even worse, allowing them to break contact from even aerial pursuit. People should not be that stealthy on horseback. It should not be legal.
</p>
<p>Still, the worst casualty of this phase is the exhaustion. Many of the troops are not used to functioning in this hot of an environment with this aggressive of an operations tempo. It proves taxing on the troops constantly scrambling for raids, and eventually they have had enough. Dust proposes a daring plan and Shadimon and Terror agree, though Shadimon goes to attempt to deliver an ultimatum of some kind to the Coyotes first.
</p>
<p>Regardless, a plan like this has a narrow window of opportunity. The fire elves and dreamdust elves cooperate to rig the ambush caravan. The dreamdust elves are a little antsy about setting so many things up to burn, but the fire elves are pumped. 
</p>
<p>The apparently undefended caravan is sent out. Winged elf rangers spread out to watch for an attack. Dreamdust elves prepare for an ambush and to set the fires. Fire elves prepare for what fire elves do. The Terror is practically vibrating.
</p>
<p>The attack comes suddenly, with the Coyotes coming straight at the caravan at full gallop. This attack is not as coordinated as the others, with instead the Coyotes attacking in a massive formation that runs at the head of another massive dust storm. You can see banners with Coyotes embroidered at them flying among the cavalry charge. You may  have underestimated them.
</p>
<p>They hit the caravan in a ragged formation. Dreamdust elves leap from the wagons, apparently fleeing the attack and leaving the caravan unattended. Coyotes rip open the covers of the wagons to find… fire. Honestly it’s a perfect setup. It could not have gone closer to plan.
</p>
<p>The fire elves erupt from the flames. The Terror, in a blur of fire and chain, leaps from the cart and takes charge of the battlefield. With the shock of erupting flames and close combat with anti-cavalry soldiers, the Coyotes are thrown into disarray. The dreamdust elves return to the fray and combined with the Charred Eagles attempt to encircle the Coyotes.
</p>
<p>The Terror and her hoplites surround a group of Coyotes - about ten. They call for the Coyotes to dismount and surrender.
</p>
<p>One of the Coyotes yells in response: “You’ll get my head off my shoulders before my ass off my horse, pendeja!” 
</p>
<p>The trapped Coyote riders then proceed to charge the Terror. Not the Elect surrounding her, not in a breakout. They charge her personally. All ten of them.
</p>
<p>Terror, with the help of her Elect, is able to survive the attack and still be standing. Most of the trapped Coyotes are killed. Two escape on horseback. Two horses are captured, along with the  Coyote who yelled to start the attack. Terror appears to have gone out of her way to leave that guy alive, and he disappears into fire elf custody.
</p>
<p>The remaining Coyotes retreat from the attack in disarray. They appear to have been already prepared to retreat, and escape full encirclement. They organize and pull their usual disappearing act, but the winged elves notice something off. There are significantly fewer Coyotes than they thought. They appear to be riding in a loose formation, specifically intended to make it look like there are more of them than there actually are.
</p>
<p>After conferring with the assembled military leaders, the cause is clear. This is a distraction. The Coyotes wanted you to think that they have a full division active in this region, when clearly they do not. If that division that attacked the nomads isn’t hear, though, where are they?
</p>
<p>Oh. Oh no.
</p>
<p><i>To: All; From: Baijani: Asavardi under attack by Coyote elves. Casualties, hostages, looting in progress. No assistance expected now, but revenge would be lovely. Fuckem.</i>
</p>
<p>Baijani is right. There’s no way you could reach Asavardi in time.
</p>
<p>Worse news comes back. The scout that travelled with Shadimon returns, alone and injured. By apparently sheer horrifically bad luck, Shadimon was shot down. The scout last saw the scout getting a net thrown over him and dragged to the ground by Coyote riders at Coldwater. He has been captured.
</p>
<p>Though they did serious damage to the attacking Coyotes, your Division was under significant stress keeping up with the Coyotes constant raids. They have the Fatigued condition until the end of Summer.
</p>
<p><b>Mental-</b> (Note: You had some help here! The Machakw decided to cooperate with you and roll their own Mental dice to try to assist you. They got two successes!)
</p>
<p>The Machakw are more than happy to tell you what they know about the Coyotes. The trick turns out to be relating that to your own experiences. They only have their own oral traditions and histories to go by, and their idea of time is a little different than yours. Regardless, you’re able to hash out the basics.
</p>
<p>During the empire, this was a frontier land with mostly large ranches and dry farms. Coldwater was a Lycan fort that was a critical staging point for their wars with the Hobgoblins to the south. The farmers and ranchers in this area seem to have been heavily taxed by the Lycans or possibly had heavy quotas for food production. The Machakw seem to use “Coyotes” to refer to the ranchers and farmers at this time, though it may also have referred to a force of auxiliary cavalry that the Lycans made from local horseriding traditions..
</p>
<p>As the rebellion reached its height, the hobgoblins pushed through the mountain passes to the south and began to take territory from the Lycans. At this time, the local auxiliaries changed sides and joined forces with the Hobgoblins. When most of the ranchers and farmers joined them, the area was considered in open rebellion.
</p>
<p>The Hobgoblins pushed quite far into this part of the world, reaching as far as Fort Alfyr. The war was quite brutal, according to the Machakw. The Lycans razed farms as they went, causing a severe food shortage. Sometime around the battle of Skaplyndi, Lycan forces retreated from this  region entirely. Machakw don’t know what happened to them after that.
</p>
<p>Around this time, the Coyotes started fighting the Hobgoblins. The Machakw don’t know why this happened, only that the fighting and food shortage got worse. This is when many Machakw died, both from hunger and from being killed in desperate raids for food. The Coyotes fought the Hobgoblins for years, but then eventually the Hobbs pulled back past the mountains.
</p>
<p>Ever since, the Coyotes have been centered out of Fort Coldwater. They are apparently slowly building back their own society and territory, rebuilding farms and ranches. The Machakw note that these Coyotes are much more violent than they used to be before the war.
</p>
<p><b>DECISION-</b> The Fate of the Machakw
</p>
<p>As Dust and Pebble discuss the Coyotes with Chief Hashkeh and Lee’Shaa, an argument breaks out. Hashkeh and Lee’Shaa have very different opinions on what happens to the Machakw. It all centers around this: They saw you burn the caravan. They saw you burn Coyotes. They realize you are taking the fight to them.
</p>
<p>The Machakw have two units worth of warriors available, one is level 3 Slayers, one is level 4 Barbarians. They are tough and hardy fighters, and itching for a chance to hurt some Coyotes.
</p>
<p>Chief Hashkeh argues that the Machakw need to be there for the fight. The Coyotes have done terrible harm to the Machakw, driving them to the brink. The Machakw spend their evenings cowering in fear, waiting for another Coyote attack. They move their settlements, hide in the dunes. They need to win.
</p>
<p>Hashkeh is grateful for the help the Dreamdust elves have given the Machakw. However he notes that there is more than one way to suppress a culture. He believes that if the Machakw do not fight and instead cower while the elves save them, they will eventually become nothing more than pets to the elves. He argues that to regain their confidence as a people and to remember themselves fondly in their histories, the Machakw have to participate in the war against the Coyotes. Not guard duty, not just scouts, they need to fight.
</p>
<p>Lee’Shaa has a much more practical objection. These two units of soldiers represent almost the entire young adult male population of the Machakw. Other than pathfinders and scouts, there are barely any young men left outside of this group.
</p>
<p>If enough of these warriors are killed in battle, the Machakw will never be able to recover. Their population will slowly dwindle and die. Pride and heroism are good, but not to the point that it gets your whole civilization wiped out. It doesn’t matter if the Machakw can look back with confidence if there are none of them left to look back.
</p>
<p>The choice is yours. If you agree with Hashkeh, you will get a Machakw Warriors division with sorely needed warriors under your control. If they suffer enough casualties however (both units at heavy casualties, or one unit decimated or wiped out) the Machakw may be doomed long term. If you agree with Lee’Shaa, the Machakw are safe but may start to see themselves as totally reliant on you. In time, it may in fact deal irreparable damage to their culture. What happens to them after the war is up in the air either way.
</p>
<p><b>Congratulations!</b> You have survived another year in this strange and chaotic land! As a reward for your perseverance, you receive one skill point to distribute as you wish!
</p>
