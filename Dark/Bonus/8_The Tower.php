<h1>The Tower</h1>

<p>“What have you done?”</p>
<p>She frowns at the accusatory tone, turning to look at the speaker.  “Domawit.”  She uses her body as a shield to keep his eyes from staring at the elves behind her.  They are scared enough.  They do not need him making it worse.</p>
<p>The Domawit is also frowning at her, his gaze indeed trailing to the surface elves behind her.  He meets her eyes once more and there is a fury there, deep set and poisonous.  “I repeat.  What have you done?”</p>
<p>She comes to a resting position, hands behind her back, but she stands tall.  She stands defiant.  “I am leading these surfacers to a safer environment than where I found them.”</p>
<p>The fury darkens.  “I forbade you from taking such a course of action.  Their business is none of ours.”  He eyes the surfacers again.  “Their <b>problems</b> are none of ours.”</p>
<p>A fury in her chest rages to fight against his own.  She keeps her hands stiffly behind her back.  “So you would leave them to suffer, when we can easily help ease some of their pain?”</p>
<p>“They are not <b>our people</b>.  They are not my concern.”  His voice is cold.  His voice is always cold.</p>
<p>Her jaw clenches.  Her hands twitch.</p>
<p>The first cracks in the tower begin.</p>
<p>She makes her choice.  “Scouts.”</p>
<p>Her group moves to attention at her call.  She is their Scoutmaster, but she will not force them to do this if they do not wish to.  A Domawit’s ire is not something she wishes upon them.</p>
<p>As an insult, she looks away from her Domawit, giving her gaze to her scouts.  “Those who do not wish to are free to go about their business, however I request that someone lead these elves to Tunnel Pass Xia where hopefully there is no surface fighting occuring.</p>
<p>There is a pause, a moment where she thinks they will all scatter, until Trishcal - bright, bright Trischal - touches the mark under her eye in salute.  “Yes, Scoutmaster!”  The others follow swiftly, their voices ringing in her ears with their conviction.</p>
<p>When she turns back, the Domawit has a tight look upon his face.  He does not stop the scouts as they begin to move the surfacers onwards; he stares at her with pointed purpose.</p>
<p>Slowly he moves forward until he is aggressively in her space, his breath upon her face with every shuddering hiss he takes in and lets out.  She refuses to flinch back, to show him a weakness or imply that she was afraid of his judgement.</p>
<p>Something had to change.  They could not keep on like this.</p>
<p>“Very well, Shyrendora.  You win.”</p>
<p>He leans closer, a violation of her space and an insult of his own.  She wraps a hand tightly around her wrist to keep from lashing out as his nose almost touches hers.</p>
<p>His eyes burn and burn.</p>
<p>“You are the death of our people.”</p>
<p>The cracks grow.  Mortar crumbles under stress and time, waiting for the right moment.</p>
<p>Finally, it gives and comes crashing down.</p>
<p>She can sense his gaze even across the cavern as it crumbles and breaks before her.  She can feel the silent anguish of the people behind her as keenly as a blade through her ribs.  She has to move them.  She has to move them or they will all perish by standing still.  She breathes around the pain, the fear, and turns sharply to everyone.  “We must move.  This tunnel will not be stable for long.”</p>
<p>So many eyes look to her.</p>
<p>Anger, grief, hope, determination.</p>
<p>Her heart drums with echoing words, ones she knows that she will never be free of.</p>
<p>“<b>You are the death of our people.</b>”</p>
<p class="center">(Fin)</p>
