<p><strong>Political</strong></p>
<p>The Ashwalker&rsquo;s speech is a triumphant success. Her speech at the Agora becomes the stuff of legend, perhaps repeated and analyzed generations later. The cheer it produces in the crowd is evidence enough for now, as well as the result.</p>
<p>The Ashwalker&rsquo;s idea for a rapid response militia gets off the ground rapidly. Unlike last time, there is no shortage for volunteers. The lines for signing up extend into the streets. In a few days time, the <strong>Civile Praesidium</strong> is formed.</p>
<p>The Praesidium, as Tsun imagined, is not a military unit. Members of the Praesidium are still involved in their civilian jobs. True to her plans, the Praesidium becomes an outlet for the rage of the civilian population. By channeling their energy with set schedules, orders, and command structures the Ashwalkers are able to turn the wolf hunting parties into something actually constructive. The Praesidium becomes a supplemental force able to be called up in times of need. They aren&rsquo;t soldiers, but they make good town guards and can be called up in an instant.</p>
<p>To say that they are loyal to the Ashwalker is&hellip; an incomplete statement. Their command structure is tied to the fire elves&rsquo; normal military, but the Ashwalker gains an enormous amount of public support from their formation and receives much of the public goodwill for &ldquo;responding to the crisis&rdquo; while the Hunger is locked in his office and the Terror is well&hellip; the Terror. The Triumvirate is far from overthrown, but the Ashwalker is clearly on the rise.</p>
<p>Any attempts to recruit an Ashwalker military unit next turn get a <strong>bonus die.</strong></p>
<p>In addition, add the Cultural Aspect <strong>Civile Praesidium</strong> to the fire elves. This is a system that will be copied in basically any settlement the fire elves have, and as such is not a settlement or terrain aspect - and perhaps not completely under your control.</p>
<p><strong>Military</strong></p>
<p>Your Pathfinders return from the hills as scheduled, but they have had casualties. They were able to map the targeted areas. They don&rsquo;t find anything wildly exciting, but it seems these mountains are rich in common minerals such as iron, tin, zinc, and a few other mundane materials. While none of these are huge veins, mining is possible in any of the mountain squares. It is also quite promising of greater mineral riches further into the mountains.</p>
<p>That&rsquo;s where the problems begin. Apparently, there was a rockslide during their return journey and several experienced Pathfinders were killed. It was a freak accident that no one could have anticipated, but the damage to the morale and the institutional experience of your Pathfinders is sharp.</p>
<p>Any use of your Pathfinders next turn will be done with <strong>difficulty upgraded</strong>, as they are scrambling to replace experienced leadership.</p>
<p><strong>Mental</strong></p>
<p>The survey of the mineral wealth of the burning maar is interesting, to say the least. First of all, Sulphur itself isn&rsquo;t terribly valuable but when used carefully it can have many purposes. Indeed it can be a fertilizer but simply mixing it up as plant food isn&rsquo;t enough as in its raw state it does nothing for plants, but composting it with rich soil can make it far more nourishing.</p>
<p>Perhaps more exciting to the fire elves, Suplhur is extremely flammable. It is used in quite a few alchemical compounds that burn, including alchemist&rsquo;s fire, and can also be used to make acids. Suplhur can also produce different color flames when it burns, depending on what it's mixed with, and could be useful for decoration.</p>
<p>As far as other minerals, the geologists find that the Maar contains many minerals and salts common to volcanic areas. None in such abundance as the sulphur, but many of them could be valuable as spell components or in alchemy.</p>
<p>Speaking of Sulphur being flammable&hellip; An experiment determining the flammability of Sulphur returns with the results of &ldquo;considerably more than we thought.&rdquo; Perhaps ironically, a fire breaks out in one of the geologist&rsquo;s central research laboratories - which at this point is his home. House fires aren&rsquo;t normally a problem for fire elves, but unfortunately they <em>are </em>a problem for delicate lab equipment, books, notes, and isolated samples.</p>
<p>At this point they have to start over again and go collect more samples. This won&rsquo;t be a problem in the long term, but research checks next turn relating to minerals take a <strong>setback die.</strong></p>
<p><strong>Event Resolved: Wolf Hunt</strong></p>
<p>Ashwalker Tsun has essentially single handedly solved the crisis with the creation of the Civile Praesidium. Nearly overnight, the roving mobs of angry elves become an organized force. Over the weeks with no sign of werewolves, people calm down.</p>
<p>Due to her rapid and effective response, Tsun is the one remembered for resolving this crisis. The fact that most of the Triumvirate were not seen directly addressing the issue is not lost on many. The Ashwalker political block grows in influence.</p>
<p>The good news is that Tsun resolved the issue so rapidly that you do <em>not</em> take an economic stress this turn. Further, with the event resolved, your stress points will start recovering naturally next turn.</p>
