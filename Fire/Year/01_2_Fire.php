<h3>Fall Year 1 Results</h3>
<p><strong>Results-</strong></p>
<p><strong>Economic- </strong>Despite the slowly increasing chaos in the city, the urban planning committee is dedicated to the potential crisis they are trying to resolve. Thankfully, the committee appears to have been burned down to those who are fully devoted to the long-term goal of making Surt a sustainable location. Even despite the&hellip; <em>rumors</em> about the Hunger&rsquo;s encounter with the werewolf. Indeed, the Hunger throws himself into this work completely, and doing so seems to ease his mind and allow him to put the experience behind him. Mostly, anyway.</p>
<p>The plans for the Aqua Via are drafted, and the system begins to be constructed. The Hunger has some truly lofty plans for the system, but for the moment only the basics are worked out. The Hunger&rsquo;s basic knowledge of alchemy seems to work out, however- one of the most important components of the system, the filtration, seems to work. Research reveals that the waters of the Burning Maar are mildly toxic. Not enough that brief exposure is a concern, but decades of exposure could be dangerous even to the fire elves.</p>
<p>The Aqua Via is constructed. Two large waterways connect both the maar itself and runoff from the foothills into Surt. The rooftop gardens seem to be struggling despite the addition of Sulphur, but the water is potable and the system actually seems to cool the buildings that it is placed in. Even fire elves can get heat exhaustion, after all.</p>
<p>Surt gains the Aspect <strong>Aqua Via</strong> in its second slot. It&rsquo;s a primitive system for the moment, but it works and by the committee&rsquo;s estimate is a big step on the road to Surt becoming sustainable.</p>
