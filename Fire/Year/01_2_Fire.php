<p>As evidenced by the local terrain, ash-heavy soil is not effective farmland. However, as the Hunger knows from his own research, there are a lot of components within ash -- nitrogen, methane, sulfur&hellip; -- that are known to increase plant growth with proper controlled exposure. And while immediately accessible water isn&rsquo;t really available from ground level, the mountains and geysers provide plenty of potential for unusually sourced aqua. So why not try something unusual?</p>
<p>Coordinating with his architects, the Hunger proposes an &lsquo;aqua via&rsquo; -- an addition to the existing walls and structures that will function as an irrigation system to stacked plants and gardens built directly onto the roofs of local buildings. The water would be sourced from both mountain runoff and nearby geysers, and filtered via fresh, low-sulfur ash. Combined with fertilizers enhanced with the local ash, this should result in a permanent source of water and farmland that can expand along with the city.</p>
