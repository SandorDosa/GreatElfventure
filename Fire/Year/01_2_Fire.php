<p><strong>Event: Wolf Hunt</strong></p>
<p>Your people are afraid, and fear makes them angry. The old monsters are back. Fire elves tend to cover up fear with rage, and this is no different. Average fire elves are taking to the street in large numbers, &ldquo;hunting&rdquo; for hidden werewolves. These groups are starting to become large and organized, and aren&rsquo;t accomplishing anything other than wrecking up local businesses looking for wolves and taking people away from vital jobs.</p>
<p>You take one point of Morale stress from the howl. In addition starting next turn you will take one point of Economic stress damage every turn until the populace is placated.</p>
