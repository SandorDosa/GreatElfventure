<p>After his bite, the Hunger consumes his prescribed dose of wolfsbane. There is nothing more to be done- there are no clerics of level 12 that could reach him in time, even if they wanted to help him.</p>
<p>The Hunger has a simple test for Lycanthropy. After resting until his injury is healed, he takes a simple phial. A dagger and a funnel are enough to capture an amount of his blood- not a great amount, but enough to be clearly visible.</p>
<p>He creates a mixture of powdered dried wolfsbane and a small amount of extremely valuable powdered silver. Mixing this with the blood of a Lycanthrope, along with some reagents to amplify the effect, will cause the blood to blacken and turn into a soupy mess.</p>
<p>The Hunger prepares the phial. He pours the mixture into his blood, then swishes the mixture for several minutes. He lets the mixture settle. He realizes he is holding his breath.</p>
<p>The blood in the phial stays bright red.</p>
<p>He has plenty of blood, it could be a false result. He repeats the process several more times. The results are the same, each time. He realizes it is now dark outside. He feels light headed. He sets the phial down and collapses into a chair.</p>
<p>The Hunger is exhausted and mentally drained by his experience, and still wakes up panting after nightmares of snapping teeth and executioner&rsquo;s swords several times a week. That, and after his bite was witnessed by so many rumors persist that he has been turned. Any <strong>Political </strong>actions involving the Hunger next turn will receive a <strong>setback die</strong>.</p>
