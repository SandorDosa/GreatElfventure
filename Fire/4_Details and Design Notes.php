<p>Religion in Surt:</p>
<ul>
<li>Cults of Calaestros:</li>
<ul>
<li>The Grasp</li>
<ul>
<li>A small and quite sneaky cult devoted to Calaestros in her aspects Chaos and Secrets (and also Insanity), this cult depicts Calaestros as a shadowy, feminine elf with a dozen arms, reaching out to grasp her children. Statues, carvings, and &lsquo;tags&rsquo; featuring her are quite common along the city&rsquo;s docks and warehousing districts, though most suspect the cult itself is very small.&nbsp;</li>
<li>The cult is believed to have originated as a minor Calaestrian faction devoted to protecting the faithful from &ldquo;eldritch influences&rdquo;, and some of their texts suggest their original task was to fight off the encroaching influence of Aboleths, Kraken, Mariliths, and some other suspiciously multi-limbed outsiders. This has led to accusations of &lsquo;alien influence&rsquo; among cult members whenever the cult splinters.</li>
<ul>
<li>In Muspelham, feuding Grasp splinter cults tended to be more willing to associate or support each other than usual -- often citing &ldquo;Lycan oppression&rdquo; as &lsquo;a greater threat that merited tolerance of the enemy within in the name of stopping the enemy without.&rsquo; This opinion varied dramatically -- more than one arm declared even formerly allied arms as &lsquo;infiltrated and lost&rsquo; when said allies shared resources with &lsquo;false arms&rsquo; -- but, perhaps due to the greater resource pool, relatively few of the &ldquo;isolationist&rdquo; arms survived to Muspelham&rsquo;s fall.</li>
<li>The true origin of the Grasp is exceedingly difficult to identify. Either deliberately or indirectly, much of the records of Elven religion before the Lycan Imperial Expansion period have been lost. However, several Arcane University excavations into theorized ancient Elven cities have revealed signs and symbols consistent with early Grasp activity. Oddly, most of these symbols are not featured in major temple sites, but rather more commonly found in ancient barracks, along significant trade routes, or on recovered Elven sailing vessels from the time.</li>
</ul>
<li>Grasp splinter cults (sometimes referred to as arms; arms that dissociate from each other will refer to the dissociated group as &ldquo;lost&rdquo; or &ldquo;false&rdquo; arms, depending on how the nature of the split) tend to deliberately deviate their rituals and designs following a split, in order to help detect &ldquo;infiltrators&rdquo; from the previous cult or elsewhere. Even so, some basic themes remain consistent:</li>
<ul>
<li>Champions -- almost every Grasp cult has beliefs and rituals surrounding supposed &ldquo;champions of the faithful&rdquo; or &ldquo;champions of the Grasp&rdquo;, who are typically powerful figures of the faith who arise in small numbers every generation to face off against the enemies of the faithful and deliver the faithful from the evil of the time. In Muspelham and early Surt, this most frequently meant &ldquo;Lycans and their supporters&rdquo;, but results varied based on what the &ldquo;Arm&rdquo; itself focuses on (sometimes resulting in aberration or vampire hunters, or the like). These champions were said to be blessed by Calaestros themself, and often granted powers that enabled them to heal the sick or injured, &lsquo;assault the wicked&rsquo;, and stand fearless against the foes of the &lsquo;righteous&rsquo;. Some Arms insisted that Champions arose naturally in the local populace, and had to be found, or that true champions only arose at the time of the faithful&rsquo;s greatest need; others tended to declare important figures within their ranks as champions, sometimes resulting in ten or more champions within a single arm.&nbsp;</li>
<li>Symbology -- while central depictions of Calaestros varied across cults and across time (particularly since some cults relied on mobile, concealed, or disposable instruments of faith), the general symbol of Calaestros as a multi-limbed silhouette reaching out with twelve spiraling arms remains relatively static, and Grasp cultists with the capacity for divine spells almost all use this depiction as the basis for their holy symbols</li>
<li>Puns -- for reasons unclear, a non-insignificant portion of Grasp liturgy and ritual focuses heavily on puns. Modern experts speculate this is a mundane effort to weed out or disrupt aberrations or fey, which studies suggest may be sensitive to or have trouble interpreting complicated linguistic humor</li>
<li>Nautical themes -- fish or oceanic themes are also exceptionally common among Grasp arms, with many of them taking particular nautical monsters as their &ldquo;patron&rdquo; or using nautical imagery to subtly indicate their presence. Cephalopoid creatures are exceptionally common here, but sea elves or even sahuagin are not uncommon either.</li>
</ul>
</ul>
<li>Cult of the Shattered Mirror</li>
<ul>
<li>The smallest real cult of Calaestros in Surt, the Cult of the Shattered Mirror is an exceptionally traditional Calaestrian cult, and was thus the primary target of Lycan anti-Calaestrian oppression within Mispelham for hundreds of years. Lacking significant access to lore, influence, or population throughout most of its history, the cult has very little influence, and relies on door-to-door work and acts of charity in order to get followers and support. Many suspect that the cult was effectively a &ldquo;Potemkin circle&rdquo;, allowing other Calaestrian cults to flourish at their expense.</li>
<li>The cult has seen something of a resurgence in Surt, and as such has been the primary target of the more anti-Calaestrian Ashen factions.</li>
</ul>
<li>Caelesteral</li>
<ul>
<li>Skilled in secret Calenary arts, the Caelesteral are a secret society of chefs, gourmands, and farmers dedicated to Art and Secrets (with a dash of Health and Trickery too). Their area of artistic expertise is food. Servants of the Caelesteral are dedicated to the worship, study, and creation of the finest (and most daring) foodstuffs they can. This cult mostly avoided Lycan oppression before the war, and as such retained much of their techniques and ideas, as well as seeds from some of their more exotic growables. Sadly, many of their recipes rely on spices, techniques, and even *diners* that are no longer available (&ldquo;What is the point of creating Soup au Raven when there are no Wereravens to enjoy it?&rdquo;), and the remaining Caelesteral have been forced to switch to simpler concepts more popular with the post-slave palate.</li>
</ul>
<li>Microcults (10 people or fewer)</li>
<ul>
<li>Family cults, guild cults, traveler shrines, cargo and transportation cults, &lsquo;altruism circles&rsquo; -- Mispelham was home to dozens of tiny cults, sheltering from the Lycan authorities by being secretive and tiny, and very carefully screening entering parties. Post-Mispelham Surt has *some* of those old cults; many of them died or were abandoned during and after the Conflagration&rsquo;s fire. Most were devoted to Health and/or Pride, though Chaos, Seaships, and even Insanity were far from uncommon. Some were &ldquo;champion cults&rdquo;, boosting Calaestros as the key to freedom from slavery, and/or pushing individual elves to champion the defense of their people and the cause of freedom; if any microcults were likely to produce divine spellcasters, it would have been these.</li>
</ul>
</ul>
</ul>

<ul>
<li>Cults of other gods or pantheons:</li>
<ul>
<li>Emperyan: the Ghoulstars</li>
<li>Drakaulos and Bronix: Cult of Liberation</li>
<ul>
<li>Sitting halfway between an anarchist cult and a larceny-oriented street gang, the Liberators are a band of ne&rsquo;er-do-wells committed to their identities as outcasts and freethinkers. They originate from any of a number of different street gangs from Mispelham, as well as &lsquo;converts&rsquo; from post-Conflagration elves (not all of them Fire Elves) whose experiences during the war pushed them towards the &lsquo;free&rsquo; life of an outlaw the Cult of Liberation offers. In a rather unique combination of Drakaulos and Bronix theology, the leadership of the Liberators is chosen from the strongest and bravest of the gang, with the stipulation that whoever is cunning enough to &lsquo;fake&rsquo; being the strongest or bravest clearly deserves the spot as well. This expresses overall Liberator philosophy -- nothing is free, everything worth having must be taken, and the only true freedom is what you take from those who would put themselves above you.</li>
</ul>
<li>Saint Errigal: Wild Healers</li>
</ul>
<li>The Ashen:</li>
<ul>
<li>Firedancers</li>
<ul>
<li>Formed by the remnants of a Calaestrian cult devoted to Art, Health, and Pride, the Firedancers are one of the loosest and most &lsquo;free-thinking&rsquo; Ashen sects, with relatively little presence among the Praesidium. Their philosophical argument is that the Conflagration ended their old lives as Blade Elves, and preserved them beyond the pain and death it presented; therefore, they owe their lives, health, and freedom it, and should &ldquo;live the fire as an extension of &lt;their&gt; being&rdquo; (to quote Agrippina Wildstorm, one of the most prominently known members). The more religious among them tend to affiliate themselves with Bronix. The Firedancers are most commonly known for their public &ldquo;firedancing&rdquo; rituals, which have been cited several times (mostly by the Praesidium) for &lsquo;violating public decency&rsquo;, but have a very strong public following; firedancing started as a thing very soon after the Conflagration, when morale was generally very low, and many Fire Elves view it as &lsquo;ritualized skinny-dipping for the young and drunk&rsquo;.</li>
</ul>
<li>Ashwalkers</li>
<ul>
</ul>
<li>Ashcast</li>
<ul>
<li>A relatively small faction of Ashen, the Ashcast follow some philosophical arguments on &ldquo;rebirth&rdquo;, based around the idea that the Conflagration took the crude materials they were as slaves and &ldquo;cast&rdquo; them into a newer, stronger form, with the ash of Mispelham as their mold. Devotees tend to be militant, and are often soldiers or guards by profession; the Ashcast are particularly prevalent within the elite soldiers of Surt. Its notable that the Ashcast are only somewhat aligned with Ashwalker Tsun, as their loyalty is to the establishment (and the Terror in particular), they tend to be more accepting of Calaestrians (and champion cults), and they compete with the Ashkin for prospectives.</li>
</ul>
<li>Smoke Jaguars</li>
<ul>
<li>Arguably one of the most violent Ashen (despite a strong anti-authority streak that keeps them generally out of the Praesidium), the Smoke Jaguars are a sect of sneaks, scouts, and would-be assassins devoted to an &ldquo;all is dust&rdquo; mindset, with some even arguing that the Fire Elves are *already dead* (strong Hekahuh influence here) and all that is left for them here is penance, or nothing here truly matters, etc etc. That &ldquo;all is dust&rdquo; mindset served well during the war, where forefront members of this sect independently operated within Lycan lines to assassinate or disrupt auxiliaries and supply line managers, usually with little evidence left behind. Their leader, Kahn Madcat, is rumored to prefer to kill his victims by tearing their throats out with his teeth.</li>
</ul>
