<ul>
<li>Bread slices coated in soft cheese and jam, meant to be toasted while eating</li>
<li>Stuffed banana peppers (cheese, meat, tomatoes/tomatillos, or fruit in certain desserts), traditionally cooked and eaten immediately</li>
<li>Chicken basted in a thick chickpea mole sauce, and grilled or fried in oil</li>
