<li>Bread stuffed with raisins, rosehips, black or redcurrants, and/or sunflower or sesame seeds; these are frequently &ldquo;handprinted&rdquo; by the baker as a baker&rsquo;s mark / seasoning effect, with some bakers frying salt, sesame seeds, cheese, or honey into the bread itself</li>
