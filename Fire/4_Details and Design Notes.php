<p>Surt&rsquo;s inn, <em>the Howl</em>, year 1:</p>
<ul>
<li>Owner: Phobos Tallflower, bard, writer of the extremely popular ballad <em>Leafstorm</em></li>
<li>Exterior: Grey-black concrete slabs (high rubble content), with baby vines of black raspberries, elderberries, and desert apricots hanging down from the roof (nothing ripe)</li>
<li>Internal: Thin white plaster over the walls and supporting columns, featuring "scintillating" (fairly tame) frescos over larger wall-segments (especially important areas)</li>
<ul>
<li>Raised stage towards the back</li>
<li>Dimly lit by the charcoal grills and the occasional torch; patrons often "light up" while there, as many Fire Elves consider it relaxing</li>
<ul>
<li>In fact, being "dim" is starting to get associated with being anxious or on edge</li>
</ul>
</ul>
<li>Furniture: Concrete lounging sofas, with thin cushions and blankets for padding, set around circular stone tables with charcoal grills at their center</li>
<li>Cuisine (limited to what can be made from travel food): Spiced cheeses, honeyed flatbreads, figs cooked in saltpork, salted nuts</li>
<li>Drinks (very little local, yet): Rye beers, so-called 'steam beer' (cooled and fermented on the rooftops), elderberry wine, berry-enhanced ales</li>
<ul>
<li>Expensive drinks: "agave milk" (knockoff pulque), stolen Lycan spiced white wines and meads</li>
</ul>
<li>Plumbing: back troughs, which are emptied and hauled to a burning pit every morning</li>
<ul>
<li>Lots and lots of graffiti on the walls, charred into place by drunk patrons</li>
</ul>
</ul>
