<li>Yogurt, sweetened with honey and berries, figs, or fruit</li>
<li>Firespiced dipping honey, a variant of Lycan spiced dipping honey with a smokier flavor and more hot peppers. Generally set on a table and used as a dip for bread, fruit slices, or meats.</li>
