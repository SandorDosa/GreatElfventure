    <p><b>Misc.</b></p>
    <p>Shadimon is going to send a few people back down the river to check in on the river elves and see if they need help. Also to tell them about the settlement we found, and warn them to keep an eye out for trouble.</p>

    <p><b>Economic:</b></p>
<p>“We have shelter. Even if it takes longer than we hope to do construction, we still have our tents. So, the next big thing is food.”</p>

<p>“How are our supplies looking?” </p>

<p>“Fine for now. We were able to make plenty of trail rations while stationed at the fort, but a promise of fresh food would make a difference. It’s late in the season, but we should be able to get at least a small crop. If the weather here doesn’t get as cold, it might be much easier. The scouts report lots of animal sign, so hunting should be good.” </p>

<p>“We’ll go ahead and start staking out a location for a garden. Start at the tops of the cliffs, and maybe in the future we can run more down the walls.” Shadimon’s lips twitched, “Though I’m afraid it’s too dry for potatoes.”</p>

