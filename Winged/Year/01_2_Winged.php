    <p><b>Military: Spend 1 fate point to motivate (Aspect: Wingtip to wingtip)</b></p>
<p>“This is not something we needed,” Shadimon sighed. News of the burned and massacred settlement had spread rapidly through their fledgling village. People who had cheerfully and excitedly talked about possible improvements on the still-crude cliffside homes were now instead worrying that coming to the canyons at all had been a terrible idea. There was a small but vocal minority questioning if they should abandon their settlement and make for the coastal forests instead.</p>

<p>“We need safety,” Anasatri said, drumming her fingers on her knee. “Plenty of people have gone to gawk at that poor village, and everyone can see whatever happened was awhile ago. Most people are saying that it happened during the war, and whatever that was has gone. But a sense of safety would help everyone feel it.”</p>

<p>“We can step up our circuits around the canyon,” Varna said. “We should be able to see anything coming from a long way off.”</p>

<p>“Have people been landing at that village, or just looking from the air?” Shadimon asked abruptly.</p>

<p>Anasatri shook her head, “Just looking. No one wants to get too close.”</p>

<p>Shadimon turned back to Varna, “Scouts around our village, yes, but I also want rangers searching around that settlement. Start there and work outwards in rings. Stay in pairs, especially if anyone goes to the ground. Hopefully it’s been too long for any tracks to still be around, but look for signs of people or animals.” At Varna’s tilted head, Shadimon added, “There’s no guarantee that was done by people. For all we know there could be some kind of monster out there, and I want to know about it. Good?”</p>

<p>Varna bowed theatrically, flashing his wings, “Very good! We go at your command.”</p>

<p>Baijani chuckled at Shadimon’s grumbling as Varna left to organize his scouts. “Do you think that will help?”</p>

<p>“I hope so, but I have other plans. I was hoping it wouldn’t be necessary, but I’m going to assign our Harriers to wartime patrols again. I want us to stick this out for longer, and at very least be sure there’s danger before we start running from shadows. This way, with rangers and patrols out, we should be able to find trouble before it finds us.”</p>

<p>Using the Defensive Operations trapping of the Conventional Warfare skill, Shadimon wants to mobilize the Harrier unit to guard duty in the immediate area in and around the canyon. Rolling 1 ability die: 1 advantage, 1 successes, 0 triumphs</p>

<p>Using the Rangers trapping of the Reconnaissance skill, we are going to scout the fuck out of the area around the razed settlement, looking for any signs of people, past or present, or any potentially dangerous animals/monsters/whatever. Rolling 3 ability dice: 2 advantage, 1 successes, 0 triumphs</p>

    <p><b>Mental:</b></p>
<p>“We should do something about the settlement.” Baijani was quieter than usual; she had a lot of personality in her small frame, and was unafraid to show it. For her to be quiet was unusual and put Shadimon’s feathers up.</p>

<p>“Such as?”</p> 

<p>“The people there. Their ways were certainly not our ways, but a great many people died and were left to rot, like garbage. We should do something about that.” Her wings were tightly clamped together, the feathers slicked down.</p>

<p>“They weren’t all elves.”

“Yes? Humans were our allies too.”</p>

<p>Shadimon tilted his head, “Do only our allies deserve it? Some of them could have been Lycans.”</p>

<p>Baijani’s hands tightened on her arms, “Them we at least did the courtesy of not desecrating their wings.”</p>

<p>“Was that courtesy or revulsion on our part?”</p>

<p>Her wings shuddered. Shadimon knew perfectly well that two of her grandchildren had been killed by <i>Myrkyr</i>, Lycan dire bats and the winged elves' particular enemy. He had been the commanding officer, afterall. “I don’t know, Shadi. Not now. Ask me something else.”</p>

<p>“What are you planning?” he asked quietly.</p>

<p>“Death rites. Move the bodies if we can to give them a bit more dignity. Everyone deserves that much. Clear away the damage if we can.”</p>

<p>“You think it will help.”</p>

<p>“I do.” She turned to face him, “We lost so much in the Fire. We couldn’t even honor our dead, except in passing. This…” she took a deep breath, “these people were also refugees, and they lost everything. I think it will give our people closure. And, it will turn that village into hallowed ground, not a slaughterhouse.”</p>

<p>She leaned forward, “Take it, and claim it. Turn it into a memorial. For them. For us. For everyone who died in the War. Give us a place to pray, to hang feathers. No one fears a memorial.”</p>

<p>“If it’s safe.”</p>

<p>“That’s what the scouts and patrols are for.”</p>

<p>“Give them time to clear the area. In the meantime, be making your plans.”</p>

