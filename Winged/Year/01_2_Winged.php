    <p><b>Results</b></p>

    <p><b>Misc.</b></p>
<p>Your messengers made contact with the river elves. They report that the river elves are putting their flotilla back together at a good pace and, well… they’re building something on the water fall. They call it a 'fall hopper'. You have no idea what that actually means, but presumably it’s some kind of contraption to get them over the falls. Your messenger reports it’s a huge stone and wood machine of some kind. You’re not sure how much of that is exaggerated.</p>

    <p><b>Economic:</b></p>
<p>Making a garden out of hard, dry stone is no small task. The tops of the cliffs, like the rest of the cliffs themselves, are not exactly arable land. There’s terrain further from the wall at both the top and the bottom that might work for farming, but doing it actually on the wall proves challenging. Carving out some spots for plants would probably work, but getting enough water and sun to them proves challenging. Just getting any kind of workable soil into a spot where it would be useful proves hard enough.</p>

<p>Your first attempt at making a large garden on the cliffs fails. Still some progress gets made. Your people learn a bit more about shaping stone, and a bit more about what it takes to scrape out a life in the cliff walls. No one gets hurt, and it’s not like you’ll be critical on food for quite a while.</p>

<p>From the lessons learned by this attempt, your next use of Investment to work on the cliffs in some way gets a bonus die.</p>

    <p><b>Political:</b></p>
<p>Your watchelves seem to stay a little more on task this time. It seems that seeing the burned village for themselves may have made them take their jobs a bit more seriously. This season, there are no zany antics or people wandering off on their own.</p>

<p>Still, nothing too out of the ordinary happens. All reports are examined carefully, and all watchelves report in. No interlopers are detected.</p>

    <p><b>Military:</b></p>
<p><u>Part 1 - Conventional Warfare</u></p>
<p>The Harriers snap into their old military protocols with ease that would be a bit depressing, if it weren’t so sorely needed right now. The Harriers are a point of pride for your people. Before them, no one tended to take the small winged elves that jumped at the slightest threat seriously. After the Harriers went wing to wing with <i>Myrkyr</i> and came back alive, they’re legends among your people. Seeing the Harriers patrolling the canyons around Asavardi goes a long way towards making your people feel safer, and it also makes the Harriers feel useful.</p>

<p>Perhaps thankfully, their patrols are all uneventful. The canyons are quiet for the entire season. Whatever killed those people, it doesn’t appear to be lurking around for now. Still, having the Harriers in the air goes a long way towards calming people’s nerves.</p>

<p><u>Part 2 - Rangers</u></p>
<p>Now here’s the juicy bits.</p>

<p>Your rangers successfully cordon off the entire burned village and make a careful survey of the doomed town. They travel in pairs, carefully catalog the settlement, and then get down to the grim business of finding out what actually happened here.</p>

<p>First, the broad strokes of the settlement itself. The settlement itself is no more than ten years old, but whatever happened here occurred no fewer than five years ago. The settlement appears to have been built in a rush, from the same type of wood as the palisade. The population indeed was mostly elves, humans, and probably half-elves. They appeared to be a variety of ages, genders, and probably were mostly civilians. At its peak, the town held no more than three hundred. It appears that few, if any, escaped.</p>

<p>Second, what they are able to learn about what happened here. Thankfully, your rangers find no monsters or foul magic lurking here. It’s quiet, save for the wind. Whatever killed them was mundane - not monsters, not Lycans. Those who died were killed by arrows, swords, knives, or clubs. There are hoofprints from horses, but none from wolves or any other lycanthropes. Footprints and bootprints only, though with so much time and so many people here it’s hard to tell who was who. It appears that a battle was fought and lost here between likely local forces, and then whoever it was that was attacking the village put it to the sword and the torch. Whatever valuables were here have been looted, but it definitely appears that the attackers were trying to cause maximum casualties. Your rangers aren’t sure if that makes it better or worse than if it were Lycans. Also thankfully, whoever massacred this village appears to have never revisited it.</p>

<p>The very visible nature of your military actions do quite a bit to calm the nerves of the winged elves. In addition, the sight of the Harriers in action again is an inspiration to your people. Your next Drill check to recruit new soldiers will have the difficulty reduced by one.</p>

    <p><b>Mental:</b></p>
<p>With the canyon appearing to be safe and with the Harriers making sure it stays that way, the way is clear to do what must be done. Everyone knew it from the beginning. There was no way you could just… leave them there. No one, no matter who they were, deserves to just be left discarded in the ashes of what was their home. With time to prepare, Baijani is able to gather together a group of like-minded spiritual winged elves. At first, she is able to gather only about a dozen volunteers. No one particularly wants to have to sift through hundreds of burned skeletons.</p>

<p>Those who have volunteered know that it needs to be done, no matter how long it takes. No one knows who these people were or what deity they followed, but they’re all mainlanders. The vast majority of cultures in this part of the world bury their dead, and leave some kind of marker to commemorate the lost and sanctify the ground they rest in. There are many different ways to do this, but honestly the point is always the same. Remember the dead, and honor them. The place where the dead lie is a place to be respected.</p>

<p>Your recent history has been full of too many mass graves and cremations. Not anymore. Even though they are only thirteen total, Baijani’s group agrees: Every single one of them deserves a burial. At first, the work is slow and draining. Each person gets a burial, a brief ceremony, and a marker. Some get stone cairns, but that gets  tiring quickly. Feathers, colored flags, wooden talismans - anything they can manage to get serves as a marker. Each one of them is unknown, but each one of them gets remembered.</p>

<p>Even working day and night, the group would take months or even a year to bury all of the dead. After a week, however, things change. Word gets around that they are not in any danger, and volunteers begin to help. Many don’t even ask, they simply arrive and begin helping. For many elves who lost everything, giving another soul rest seems therapeutic.</p>

<p>One thing that is silently decided on is not to ‘save’ the buildings themselves. They are almost all badly burned, and even if they could be salvaged no one wants to live in a building where something like this happened. The volunteers take down the buildings, and use the materials to build the impromptu… cemetery, you could call it. Memorial is probably a better word.</p>

<p>Near the end of the season, the memorial is complete. There are no more bodies, no more gruesome reminders of the tragedy that happened here. What exact form that the memorial takes is up to you, but it has become a place of peace.</p>

<p>Near the end, one of the volunteers finds something that may be another piece of the puzzle to what happened here. Still pinned to the chest of one of the dead, found in one of the biggest buildings in the settlement, was a simple copper star. Engraved into the star were the following words.</p>
<p><i>Gabe Cazarez</i></p>
<p><i>Coldwater Sheriff</i></p>

    <p><b><u>Event Resolved: Are We Safe?</u></b></p>
<p>With the visible military presence and careful examination of the burned village, all but the most extreme voices are quieted. You have avoided taking another point of stress this turn, and your stress will resume healing naturally next turn. While there’s still quite a bit making people nervous and still much that has people deeply concerned, the winged elves have as a whole decided to stay. For the first time in a long time, they have a home.</p>

