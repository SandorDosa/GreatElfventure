    <p><b>Economic:</b>/p>
<p>Well, Surtian politics are a <i>thing</i> that's for sure. Your traders end up literally dropping the copper shipment straight into the middle of a serious political spat. Seriously, there's a large scale political rally going on in the Agora at the exact moment that they deliver the first part of the copper shipment, with Barrabus smack in the middle of a stirring speech.</p>

<p>Your traders are then dragged into the middle of this debate. Dutifully, they publicly declare that the shipment is to be delivered directly to the Triumvirate as a gift in thanks for the sacrifices made by the fire elves. Your traders...aren't completely sure what happens next. Barrabus makes a big speech thanking them, and they are immediately mobbed by merchants asking about more copper. The shipment indeed does make it to the Triumvirate, with thanks from the Hunger.</p>

<p>Effects on Surtian politics aside, your traders are able to make many high level contacts within the most influential Surtian merchants. They receive the thanks of a group known as the Phoenix Imperialis, apparently Barrabus' political party.</p>

<p>Well, whatever. The contracts prove quite fruitful, with Surt eager for more copper and trade. You successfully deliver the copper shipment, and all further Trade checks with the fire elves will have the difficulty reduced by one. So, a win you guess?</p>

<p>Oh hey nice! Dreamdust traders finally bring in a Textile Shipment for you! Just in time for winter, too!</p>

    <p><b>Political:</b></p>
<p>Aw, <i>fuck</i>, you hate being right.</p>

<p>Your scouts sent to Riverhaven were on point. The Riverhaven guards were on point. Everyone did their jobs right. Sometimes, though, you do everything right and things still go to shit.</p>

<p>There were several Coyote spies among the newly named Vaqueros. You caught most of them quick. However, they were literally the best spies Coyote had. One managed to escape, fleeing on a horse fast enough that even the winged elves could not catch him and disappearing well enough that Dreamdust rangers couldn't track him.</p>

<p>He escaped with a report. Coyote knows where the refugees are now. He'll be coming for them.</p>

<p>It's not a total loss. Dust is able to get the captured spies to talk. Coyote is coming, but he wants to come at full force. That means he needs at least a season for his Cazadores to recover and to call his militia together. They'll likely have a large number of troops, but they won't be coming next season. You still have a window to prepare.</p>

<p>Also on the upside you caught some of Coyote's best spies, meaning his espionage attempts will be at a penalty for a little while.</p>

    <p><b>Military:</b></p>
<p>As is typical, the pathfinders exceed at their duties. In fact they get a little further than expected, scouting a total of four territories around the watchtower. The detailed star charts provided make navigation and mapmaking significantly easier than expected.</p>

<p>Most of the terrain is desert as expected, and the pathfinders confirm the preliminary reports of the rangers. There are no routes up the Watchtower that wouldn't require highly technical climbing.</p>

<p>Most interesting, the pathfinders confirm that there is an Underground River that comes up in a cave to the northwest of the Watchtower, and the river feeds into the larger Coldwater network. Your scouts explored as far into the cave as they dared and confirmed that it heads to the west towards the canyon, but turn back before they can conclusively confirm whether or not it connects to the other underground river discovered.</p>

<p>Cave experts may be necessary, as any efforts by the winged elves to explore a pitch dark cave full of rushing water (<i>really</i> not their forte) will take an extra challenge die.</p>

    <p><b>Mental:</b></p>
<p>Congratulations, you have successfully created the civilization aspect <i>Skywatchers: Fledgelings</i>. With the many dangers Asavardi is facing, everyone agrees that discipline is needed in watching the skies. The impromptu group that defined itself in the siege of Asavardi and felt the biting sting of betrayal in its aftermath becomes a formal organization, complete with rank, duties, and training.</p>

<p>As to what that means and the actual training, well, they're working on it. They are currently actively developing the training regiments and standards they will be held to, as well as recruiting members and defining their exact role in winged elf society.</p>

<p>Though they are still learning to fly as an organization, they are determined to take their duties of watching over winged elf settlements seriously. This Aspect may be invoked on any Cloak and Dagger checks, and can be improved with further Creation checks.</p>

    <p><b>Character:</b></p>
<p><i>Anasatri:</i> <p>Anasatri throws herself into researching the star charts in a way that she never quite has before. She spends weeks on end pouring through thousands of charts within the archive, looking for ones that match her vision. The researchers have to occasionally force her to stop and sleep, as well as eat. In the end, it pays off. Anasatri finds several charts that match her visions.</p>

<p>The first matches a date approximately forty years ago, in a location somewhere far northwest in the northern mountains. Using Lycan maps, she is able to pinpoint the location as Novarsk. Unsure what it means, Anasatri takes a break to sleep. That night, she receives a vision - Anasatri experiences the events of A Brave Fool literally from Bryti's perspective. This somewhat jarring vision at least confirms her story, though Anasatri is unsure of the exact significance of it.</p>

<p>As her research continues, Anasatri receives another dream vision. This time, however, it is not from Luna.</p>

<p>Anasatri stands before a lake. She sees Luna in the sky above her, dark and cracked as she was in the vision. As Anasatri looks down, she sees Luna's form reflected in the lake. No, not Luna. It is the holy symbol of Calestros, the cracked mirror. The cracks in the mirror match the cracks on Luna exactly. As she ponders the significance of this, Anasatri hears a familiar voice filled with sorrow.</p>

<p><i>It's happening again</i>, She speaks, <i>I don't know why, but it's happening to Her. Oh, it's not right! I don't want Her to be like me. I don't want it to happen again, it shouldn't happen to anyone. I am so sorry Anasatri, so sorry for how I failed my children. She didn't do anything wrong like I did, She doesn't deserve this. Please, don't let it happen again, please make sure She does not end up like me. It's not too late...</i></p>

<p>Anasatri awakens, awash in a confusing jumble of emotions. The words were confusing, but the desperation clear.</p>

<p>Anasatri still has plenty of charts that she believes she may have identified. She has picked out one more that exists in the archive as it is now. The way in which this will work becomes clear. When she identifies a starscape, she must meditate on it. When it becomes clear, she will receive a vision from Luna related to the time and place in which the star chart communicates.</p>

<p>What purpose this serves she does not know.</p>

<p>However, there is a bit of a problem. Many of the starscapes are definitely not in the archive. This means that she will not be able to receive all of the visions until the Orrery is rebuilt and she can calculate the exact time and locations of these starfields herself.</p>


    <p><b>War Games:</b></p>
<p>Unfortunately, no one is able to spare enough soldiers for there to be true war games this season. Still, nearly everyone was able to send at least a handful of elites.</p>

<p>Breaker and a few of her Cazadores are able to demonstrate what they're capable of in open cavalry combat. Litoria and a few of the Crocodilians who aren't still recovering from their losses demonstrate some of their river ambush tactics. Dark elves demonstrate some of their close quarters fighting in the fort. Dreamdust elves give primers on night fighting and desert tactics. The fire elves are able to demonstrate shield walls and a phalanx. Shadimon isn't able to fly, but Shrike is able to demonstrate the aerial tactics they have developed with the blade elves. The blade elves show off the offensive and defensive capabilities of the swordelves. Even the mighty and terrifying Echo, made even more terrifying by horrible burn scars now covering nearly half his body, shows up with a few of his Euclast warriors and show off their brutal front line slugging potential.</p>

<p>All in all, while it is not exactly what was planned, seeing the best of the best show off what they can do is good for morale. The morale boost will provide all who participated with a boost die on the Drill trapping of Military Science next season.</p>
    
