    <h2>Results</h2>
    <p><b>Economic:</b></p>
<p>Growing Asavardi is going to be a major undertaking. The good news is that the winged elves seem quite enthusiastic about growing Asavardi into a larger settlement, the bad news is that enthusiasm alone won't cut it. While you do not succeed this turn, you do get a better idea of what will be needed.</p>

<p>One is raw population. The Dark Elves report that the number of winged elves arriving at the Hilt'Inn is growing. With word of Asavardi succeeding, more are making the trek across the badlands to join you. If a few volunteers go back to human territory to spread the word, this could bring a large enough number of refugees back. This could be represented by Invoking the Well Prepared Refugees Aspect the dark elves gave you for a proficiency die.</p>

<p>Two, this is going to require a lot of food production. Thankfully, you have spent a lot of time developing an Aspect for that. The yearly boost die from Community Gardens could be applied to this check.</p>

<p>Three is planning. The winged elves have gotten better at stonework, but it is still very difficult. It took years to get Asavardi where it is, and this would be more than doubling the size of the settlement. The Research trapping of Academics could be used to create a comprehensive plan for the expansion of the city, providing probably an Upgrade, if not more with good luck.</p>

<p>Fourth is raw materials. This will require lumber, a large amount of tools, rope, fabric...anything you could think of. You could Trade for some of these supplies, or find a way to harvest them yourself.</p>

<p>These are not the only things that could be done to improve your chances of growing Asavardi, just some examples. There are any number of clever ways to help.</p>

<p>Regardless, growing a settlement is always a major undertaking that requires planning and resources. Thankfully, the winged elves are quite clever.</p>

