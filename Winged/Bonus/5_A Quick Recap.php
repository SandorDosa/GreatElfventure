<h1>A Quick Recap</h1>

<p>Baijani dropped onto the bench across from Shadimon, "Welcome home. How was the meeting?"</p>

<p>"Good overall. It sounds like a lot of the others have had a rough year, but most are getting settled finally. The totem elves didn't show, and no one has seen or heard from them. Partager is heading the way they went and will keep an eye out for them. But instead...do you remember the mountain elves?"</p>

<p>"Hard to forget their cheerful and outgoing dispositions, she said dryly. Don't tell me they came?"</p>

<p>"They did, two of them. They live somewhere near the Fort, and have emerged again for trade. They seemed reluctant about it, so I suspect something is going wrong with them."</p>

<p>"There was good talk about potential trade and possible combined projects. Shyrendora was interested in the cave system we found beyond the waterfall. The feathers were well-received. Though the mountain elves didn't know what to make of a...symbolic offering, and the one in charge gave me that magic crystal light that's out in the main room." He paused to take a drink, chuckling slightly, "Speaking of magic, apparently Ink has some impressive magic, and offered to enchant the feathers with Sending spells so we can all talk to each other. So he should be showing up...at some point? To do that."</p>

<p>"That'll be something to see. Everyone behave this year? Any more sniping from the fire elves?" She raised her eyebrows at Shadimon's sigh, "That good, was it?"</p>

<p>"Apparently there's a fire elf faction that's even more troubled than average. Their leader, someone named Ashwalker Tsun, walked in to the party and challenged Ehra to a fight right then and there."</p>

<p>Baijani whistled softly, "And did this Ashwalker manage to walk away?"</p>

<p>"Yes, because Satri healed her before she bled out. Ehra cut the girl's throat faster than you could blink. I don't know what she was trying to prove; Hunger and Fury didn't want much to do with her, so I assume she's trouble in Surt. As if that wasn't enough, one of the mountain - sorry, crag - elves took exception to Hunger being a leader and challenged him to a word-duel."</p>

<p>Baijani put her head in her hands, shoulders and wings shaking with withheld laughter.</p>

<p>"That was my response too," Shadimon continued. "By that point the entire circus was just so ridiculous. There was some complicated process of naming terms and judges, and Hunger asked Satri to be one. You can get the full story from her, but Hunger gave the crag elf the win. In actual important news, Hunger and Litoria both reported seeing a stranger that looked like a blade elf, but turned out to be a Lycan." He nodded as Baijani tensed, "She - the werewolf - attacked Surt, but was entirely peaceful with Partager. The river elves didn't even know what she was. Blade elf with white hair and a huge silver sword."</p>

<p>"Silver."</p>

<p>"No one knows. If you'd start that through the gossip chain, please, so it's at least known. She's only been seen up to the north, and has to know that the fire elves would tell everyone else, so I doubt anyone will see her for some time, if at all. But where there's one..."</p>

<p>She nodded, "I can do that, without also causing a panic. There isn't much news here since you left. The mages have kept poking at the ward ritual, and are fairly certain they have something that will work. They plan to actually put it up soon, so that will help any concerns. Though there really may not be many. People are relaxing, finally. There's finally a sense that we're <i>home</i>." She smiled a little, "I have to say, I spent many years doubting I'd live to see it. Or fearing that I would, but none of my family." She looked down at the table as Shadimon reached across to put his hand over hers, "But I did, and we did. Sometimes I don't believe it, but then I go outside and watch and listen, to all the people making something new here, something new, and strong. We did it, Shadi. We really did it."</p>

<p>Shadimon went around the table to sit next to her, spreading one of her wings over her, "We did. Is it prejudicial to say I doubted it too?"</p>

<p>Baijani leaned her head against his, "No, because you know better than to lie to me, brancher. I think we all doubted. How could we not? But it's done. We did what we had to do. It was harsh and bitter and the worst thing our people have ever seen, but it's done, and we can fly free now."</p>

<p>"From your lips to the Goddess's ear."</p>

<p>"She hears us, never doubt that. She gave us our wings and set us free, but even She can't always promise calm winds. It's up to us to fly them, and we have. And She gave <i>you</i> the instinct to see us through that storm." She chuckled at his heavy sigh, "The Goddess loves you, Shadi, and so do the rest of us. Eventually you'll just have to accept that."</p>

<p>"Goddess forbid I try fighting with you..."</p>

<p>"Yes, because you'd lose."</p>

<p>"You have a wonderful way with words, you know."</p>

<p>"Comes from being old. I can say anything I want and get away with it under the guise of elder's wisdom. The rest of you just have to take it."</p>

<p>"Sadly, you're usually right."</p>

<p>"Usually."</p>

<p>"Frequently."</p>

<p>"Damn right."</p>

