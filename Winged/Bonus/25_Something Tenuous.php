<p>Tathariel blew in through the front door, nearly knocking over a stool someone had left nearby. A few people were lounging in the nest and looked up wide-eyed, "Goddess, Thari, what's the rush?"/p>

<p>"Sorry, sorry, is Rava here?"</p>

<p>"What did you do?" Vatsarava's voice came from the bathing room. "Whatever it was, come help me in here while you're confessing it."</p>

<p>Tathariel peeked into the room, watching her stirring laundry in the basin. "Help me wring this out so we can go hang it to dry. Now what happened?"</p>

<p>Tathariel glanced at the door and lowered his voice, "Shrike ambushed me at Shadimon's house..."</p>

<p><i>"Hello! Sorry if you were expecting the place to be empty."</p>

<p>Tathariel had indeed not been expecting anyone to be in the nest, clearly waiting for him. "Ah, it's fine. Is Shadimon here too?"</p>

<p>"Nope, which is very intentional. Come have a seat."</p>

<p>Nervous now, Tathariel approached cautiously. Shrike was a striking and intimidating sight, even lounging, with her scarred, muscular torso and her hair still shaved into a war-crest. "Is something wrong...?"</p>

<p>She grinned at him, "Very much not. But there are some things you need to know. First thing, I've moved in. Second thing, before your ears droop much further, I know that <i>you're</i> hoping to move in as well, and I'm absolutely in support of this. I wanted to talk to you so we could coordinate, and it's largely going to depend on how much of an ambush do you want to spring on him."</p>

<p>Tathariel stared, mouth open, "I...what..."</p>

<p>Shrike leaned onto the low table, raising her eyebrows, "Ama told me you were asking her for advice. You&"re still interested, yeah?"</p>

<p>"Yes, absolutely! I just...didn't expect...Several things, apparently!"</p>

<p>"Good! That means Shadi isn't expecting it either. So let's do some scheming..."</i></p>

<p>"And so she wants to help."</p>

<p>"Huh," Vatsarava vigorously wrung out a pair of pants, "Sounds like an excellent thing to me. But once again you're worried about something."</p>

<p>He reached into the basin and picked up a shirt, taking out his frustrations on it, "I feel like...it's too good to be? And I'm just waiting for the wind to die down and drop me."</p>

<p>"That's when you've got to remember that you have wings yourself, brother mine. The winds might get tougher, but you can fly them."</p>

