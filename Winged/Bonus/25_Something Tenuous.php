<p>A hand ran through Tathariel's feathers, and he barely managed to keep from leaping out the window. "<i>Goddess</i>, Rava, do you have to do that?"</p>

<p>Vatsarava, his older sister, sat down on the edge of the table, "You must be thinking hard, normally I can't sneak up on you quite that bad."</p>

<p>He raised a sardonic eyebrow at her; of the two of them, she was nearly silent and could disappear in broad daylight. "Working on a new project. I had an idea for trying to help out the moving teams. Getting things up and down the cliffs isn't easy, even with baskets, but I think I might be able to enchant the carrying harnesses so they can take more weight."</p>

<p>She tilted her head, "That would be good. You seem to be dabbling more in magic lately. Purely magic."</p>

<p>"It's more useful. And if I'm repairing things <i>anyway</i>..."</p>

<p>Vatsarava picked up the thick braided leather cord coiled up on the table, running it through her fingers, "So are you contemplating the mysteries of magic, or is there another reason you were staring wistfully out the window?"</p>

<p>He started, "What? No, just thinking."</p>

<p>"Mhmm. Your feathers are fluffed. So let's have it. What's on your mind that you think is embarrassing?"</p>

<p>He frowned at her and resettled his feathers, "I don't know why you think that..."</p>

<p>"Please Thari, I've only known you your entire life." She scooted over so she was sitting in front of him and pulled him closer so his head was leaning against her chest. "I want to tease you, but I'd like to be helping while I'm doing it."</p>

<p>He grumbled a bit, but sighed and let his wings sag, "You know I've been helping Shadimon out."</p>

<p>"Of course." She paused, "Is he...?"</p>

<p>"Doing better. While his feathers were still growing back..." He shuddered. "That was awful to see. I can't imagine how he managed." He cleared his throat, "He invited me to keep doing some of my work there, since it's quieter and less distracting than here. And he needs the company. I see why Baijani worries about him; he's so...quiet. And all alone in a silent house. He's barely done anything with the house. I think he's still living out of field packs. I don't know why he isn't...I don't know, settling in?"</p>

<p>"And you worry about that."</p>

<p>"Yes! Our family is a little much sometimes, but he doesn't have anyone."/p>

<p>"And you want to be that someone."</p>

<p>"es! Wait, I...No...I mean..."</p>

<p>She ruffled his hair, "Thari, <i>everyone</i> wants to fly spirals with Shadimon. You just might have a decent chance at it." She ignored his sputtering, "Think you're going to go for it? If you have a standing invitation to his house, you're already ahead of everyone else." She tapped her lip, "So I suppose it depends what kind of relationship you want. If you just want sex, that's much easier. Anything more intimate is tougher to do unless you actually sit down and talk, which I suspect you won't because you're so adorably embarrassed by the silliest things-"</p>

<p>"Rava! I don't...I don't know. I mean...yes of course I'd like sex; he's gorgeous. But..." He gestured helplessly, "I'd...maybe...just...like to...be family. I think. That's definitely what he needs."</p>

<p>"Ah-ah," she waved a finger at him, "What does <i>Thari</i> need?"</p>

<p>"Goddess, Rava, I don't know. It's not that I don't love our family here, but..."</p>

<p>"But other than me, you don't have really strong connections." She put her chin on his head, "We had talked about the possibility of splitting off and householding with just us, and we can do that. Unless you're going to fly for the sun and aim for the best possible prize you can."</p>

<p>"<i>Ravaaaaa</i>..."</p>

<p>She snickered, "Teasing aside, seriously, do you think you want to?"</p>

<p>"Want to? Goddess yes. But I'm not...sure how."</p>

<p>Vatsarava leaned back, peering at him, "Wow, you're serious. You've got some really strong feelings here, don't you."</p>

<p>He planted his face against her chest again, "Yes and I have no idea what to <i>do</i> with it. I've been interested in people before and just go up and talk. But this is...different, and I don't know why."</p>

<p>"So I have a suggestion for you. You need to talk to someone with better advice to give, and specifically someone who knows him. Baijani or Shrike."</p>

<p>He squawked, "I don't-! I can't-! But-!"</p>

<p>"Who else could you ask? Baijani is the one who prodded you into doing this in the first place, isn't she? And she's known him for his entire life, as far as I can tell. He and Shrike are practically siblings. They can at least tell you if it's worth pursuing, and can give you ideas on how to chase that particular game."</p>

<p>"Goddess you make it sound like I'm hunting him down."</p>

<p>"You are, but politely." She ran her fingers through his hair, tugging on the small braids, "Will you do it? It's been a long time since you've had someone for <i>you</i>, and I worry about you the same way you worry about him."</p>

<p>He sighed, "I will. Tomorrow."</p>

<p>~Tomorrow~</p>

<p>Tathariel fidgeted at the arch leading into the offices for a few minutes before peering around the edge. The actual working space had been expanded and a latticed window had been opened onto the walkway outside. A lot of the available wall space had been carved into shelves, and a collection of tables were arranged around the room. In practicality, only Baijani and Shadimon had dedicated personal work spaces; the other leaders either used the larger common table or kept their offices at their respective workplaces.</p>

<p>Baijani was at her desk; she had made a habit of spending late mornings in the office so people could find her. She had taken on the work of talking and mediating issues, since everyone came to her anyway.</p>

<p>Tathariel stepped in, rustling his wings quietly, "Baijani? Is this a good time?"</p>

<p>She looked up and smiled, "Certainly, come have a seat."</p>

<p>He paused, glancing at the curtain pulled back to the door frame, "Can I...?"</p>

<p>"Ah, of course."She set aside the things she was working on as he dropped the curtain over the door. It didn't actually keep people from eavesdropping or coming in, but it was rarely closed and people tended to respect the request for privacy.</p>

<p>Tathariel perched on a stool and settled his wings, turning one of his rings while considering how to start. When he looked up, Baijani was waiting patiently. Tathariel threw caution to the wind and bluntly said, "I can't get my mind off of Shadimon, and I want to start a family with him. My sister suggested asking you for advice, so here I am."</p>

<p>Baijani stared at him for long enough that he started to worry, before bursting into peals of delighted laughter. "Oh, you have no idea how glad I am to hear that! I've been hoping for years that someone would try to break through his shell. He comes across as so distant on the outside..." She folded her arms on the desk and leaned forward, wings lifting, "So, where are you now, and what are you aiming for?"</p>

<p>"Well, he's asked me to keep up what I've been doing at his house, and invited...asked me to bring some of my work there and just...be company. We talk, but a lot of the time it's just...quiet. As for what I'm aiming for..." He took a deep breath, "Just what I said. I want to be family. Whatever...that ends up meaning."</p>

<p>She nodded slowly and thought for a moment, "So far I think you're on your way. I know he likes you, and he's grateful for what you do for him." She tapped the table, "He's always favored quiet company. His family always prioritized presence. Not to say they never talked or anything; they surely were as loud as the rest of us. But they didn't feel a need to, if that makes sense."</p>

<p>"Yes of course."/p>

<p>"So, Shadi wants someone there, and he specifically asked <i>you</i>. Shrike invited herself to do the same thing, and he knows he's welcome with us." She held up a finger, "But he still asked you. That right there is an important step. You've certainly noticed that Shadi is unusually hands-off. He just doesn't touch as casually as most people, unless he feels closer to you. Watch for that, and that'll be your indication for how things are going." She paused, "I would advise taking it slow so you don't scare him off. He's only recently started to even consider a family again, and we want to encourage that."</p>

<p>"So you think I should be, ah, more circumspect about how I feel?"</p>

<p>"Hmm, somewhat. If you just come out and say you have feelings for him, honestly he'd probably be extremely baffled and awkward." Her voice was full of fond exasperation. "Same if you try to flirt with him. He'll probably miss half of it. I do love the boy, but he's pathetically unobservant sometimes." She paused and grinned slyly, "Though, one thing you could do that might move things along...This winter hasn't been as harsh as the last one, but it's still plenty cold. And you know that a bed just never gets properly warm with one person..."</p>

<p>Tathariel opened his mouth, paused, and blushed furiously, "Do you think he would go for that?"</p>

<p>"I think so. He's stayed with us semi-regularly, and has explicitly mentioned that he does miss having someone to sleep with. So, ask, and if he accepts, then you know you've already shifted into the category of people he's more comfortable around. He's hopelessly affectionate when you get through to him, so you have that to look forward to and take advantage of!" She grinned again, "And even if he's oblivious to your actual romantic intent, you'll probably still get to fuck him."</p>

<p>Tathariel choked and almost tipped off the stool while Baijani smiled beatifically at him, "I do so love when people somehow think I won't be crude just because I'm old. But, it seems like you have something of a plan. As for other things...just be yourself. I know he already likes you. We just need to get his head out of the clouds and get him to acknowledge it."</p>

<p>"We..." Tathariel squinted at her, "Were you hoping for this all along?"</p>

<p>She laughed and held up her hands, "I'm afraid so. I've been trying to get him with someone for decades. You're just the first one to make any kind of progress. I'll keep my nose out of it, but if the subject comes up, I'll put in a good word."</p>

<p>Tathariel smiled, trying to rub the blush off his cheeks, "Thanks, I hope...well, I hope it works out."</p>

<p>"Me too, son. Keep me updated."</p>

