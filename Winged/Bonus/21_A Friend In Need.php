<h1>A Friend in Need</h1>
<p>Shadimon tugged on the braided cord hanging outside the tent, jingling the small bells woven into it.</p>

<p>"Be welcome!"</p>

<p>Dust was seated on a tasseled pillow with a small wooden desk across his lap, which he set aside as Shadimon entered. "Perfect timing, I was just about to make tea. Join me?"</p>

<p>Shadimon dropped gracelessly onto another pillow, leaning forward with his elbows resting on his knees, "Gladly, assuming I can still have a coherent conversation after."</p>

<p>Dust sifted some leaves into a small pot and set it over a delicate brazier, "Just normal tea, I'm afraid." He flicked his eyes briefly up to Shadimon, "But if you needed something a little stronger..."</p>

<p>Shadimon rubbed his eyes, "I am considering if I might want to just sleep for approximately two days."</p>

<p>"We could do that," Dust said affably, swirling the teapot casually. Rather <i>too</i> casually.</p>

<p>"I know I'm messed up," Shadimon sighed. "It'll take time, but I think I can work through it myself. So you can stop very carefully not-staring at me."</p>

<p>Dust chuckled as he poured two cups. The tea was dark and fragrant, and he added a dollop of goat butter to each cup before handing one to Shadimon. "I'll take your word for it. Just know that help is available if you need it." There was compassion in his coppery eyes, "But I assume you didn't just come by for tea."</p>

<p>Shadimon sipped; normally their tea was far too bitter for him, but after months of drinking nothing but water, the strong flavors were very welcomed. He actually paused to appreciate how the melting butter gave it a smoother taste. "No, but the tea is appreciated. I wanted to thank you. For the dreams." He looked down into the cup, "I had accepted that I wasn't going to leave there alive. You helped me feel that, even if that were true, I hadn't been forgotten."</p>

<p>"I'm glad I could help." Dust sipped his tea. "Because of what happened, we've been working on developing two-way dream communication, so that no one will be isolated like that again."</p>

<p>Shadimon's ears flicked down in surprise, "That would be a hell of a thing. Have you made it work?"</p>

<p>"We think so. Steve has to be involved to make it work, but so far it seems to be possible. It's not terribly long, half an hour or so, but that's still a significant improvement over anything else." He grinned suddenly, "Did you enjoy the facts about goats?"</p>

<p>Shadimon laughed, quietly, but honestly, "The gossip was more interesting. <i>Have</i> Pebble and Dusk figured things out?"</p>

<p>"I assume so. There was a barricade of goats around Pebble's tent that wouldn't let either of them leave." Dust primly sipped his tea, "And it isn't <i>gossip</i>. It's social logistics."</p>

<p>"Isn't that Dave's job?"</p>

<p>"She does regular logistics." He wrinkled his nose, "Like the textiles we still owe you." Dust twitched his head when Shadimon started to speak, "Ah, sorry, not a time for business. Just one of the many things that's been on my mind." His eyes flicked back to Shadimon's, searching and far too clear, "I mean that, if you need help. Your people are good about social support, but if you need an outside perspective, we're here."</p>

<hr>

<p>"You're not sleeping well." It was a statement, not a question.</p>

<p>"It's so obvious, I assume." Shadimon sighed, "No. Even without nightmares, I'm too restless to get anything out of it." He gestured vaguely and shrugged, "I have to assume that'll improve over time."</p>

<p>"I can give you a tea to help you sleep. It would do you good in my mostly-professional opinion."</p>

<p>Shadimon paused, thinking, "I...might take you up on that." He grimaced, "I've been trying to rest. I certainly had nothing better to do but..."</p>

<p>"Not exactly conducive to getting anything out of it, no. I can give you some of our tea, but if you think you'd like it, I'd prefer you try it here first, just so we can make sure it won't do anything weird to you."</p>

<p>Shadimon blinked, "Is that likely?"</p>

<p>"Honestly? No, not at all. But you've been under a lot of stress. If anything, it's more likely to just hit you harder and you'll wake up some time midmorning."</p>

<p>"That also wouldn't be a bad thing," Shadimon said with a quiet chuckle. "Sounds good."</p>

<p>"Excellent, I'll go ahead and brew it and you can try it here."</p>

<p>"What, now? And here? Not the tent you loaned us?"</p>

<p>"Nope, here, so I can keep an eye on you."</p>

<p>"I don't want to take over <i>your</i> bed!"</p>

<p>Dust snorted and gestured to the small lap desk, "We're nocturnal, remember? I have things to do, and this will give me an excellent excuse to tie the tent flaps shut and not be bothered."</p>

<p>"I'm sensing some ulterior motives..."</p>

<p>"You would be correct. Why do things for one reason, when you can accomplish many things with one action?" He busied himself with the tea things again, "It doesn't bother you to sleep in the same room with someone else, does it?"</p>

<p>"Oh, no. No we usually sleep in groups."</p>

<p>"Very good. And your unconscious body won't be any inconvenience to me. I'll be sure to inform you if you snore."</p>

<p>Shadimon stared at him and blurted an unwilling laugh. "And this is why no one argues with you."</p>

<p>Dust held up a finger, "No one argues with me <i>twice</i>. The trick is to keep them from realizing they've lost." The new smell coming from the teapot was...exciting. "Strong, isn't it?" Dust commented. "It tastes slightly better than it smells, but only slightly."</p>

<p>Shadimon managed to not gag at the taste, but Dust watched with amusement as all his feathers ruffled. Dust took the cup back and gestured to the pallet, "Make yourself at home. You have a little time before it kicks in."</p>

<p>"You're sure..."</p>

<p>Dust raised an eyebrow, "Do you really think this is the first time I've had a strange man in my bed?"</p>

<p>Shadimon couldn't help it; he laughed until his chest ached, "I would hope I'm not that much of a stranger!"</p>

<p>Dust grinned, "Exactly, and you in particular have been in my bed before. so get settled before you fall on your face. <i>That</i> would inconvenience me."</p>

<p>That had been the best night of sleep Shadimon could remember having in a very long time. As Dust had predicted, he woke sometime in midmorning. The tent had been cozy and dim, and Dust had somehow gotten between him and the tent wall. The other elf cracked an eye when Shadimon slipped out from under the colorful blankets, but quickly went back to sleep. There was a small note sitting on the lapdesk: <i>Dave's on day-shift. Check in with her and <u>eat breakfast!</u></i></p>

