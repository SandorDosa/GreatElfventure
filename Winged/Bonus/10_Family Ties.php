<h1>Family Ties</h1>

<p> Baijani stooped the pick up a feather on the floor. "Are you molting? Wrong season for that." She stepped into the sunken seating area and leaned back against a pillow. Shadimon's home was rather more sparse than most, but he was one of the very few people who officially lived alone. That was, of course, the main reason Baijani came by regularly to bother him.</p>

<p>She twirled the shed feather between her fingers, "So. What happened." Shadimon opened his mouth to deflect the question, and closed it again at her glare. He sighed and held out his hand for the feather, adding it to a small pile on the edge of the seating area. His wings were starting to show stress in unkempt feathers, but fortunately the missing ones were small covert feathers for the time being.</p>

<p>Baijani got up, rummaging through the bedroom until she found a preening brush and a small jar of oil. Back in the den, she sat down on the edge of the nest and pulled one of Shadimon's wings over her lap and started running the brush over the ruffled feathers. The soft brush, dipped in the oil, smoothed out the rough barbs and helped strip dust.</p>

<p>Shadimon's shoulders loosened and he let his head sag forward, sighing heavily. "I'm worried about safety." The brush found and separated two tangled feathers. "That wasn't a dreamdust elf I found in Radiven's house."</p>

<p>The brush strokes slowed, "The werewolf?"</p>

<p>"The werewolf. She was stealing spices, if you'll believe it. She managed to climb the cliff walls. <i>And no one saw her.</i> For the gods' sakes, she's an enormous white werewolf, against dark red rock, and <i>no one noticed</i>!"</p>

<p>Baijani rested her wrist on his wing, "Including the ward..."</p>

<p>"I checked with the mages the next day. It's still up and functioning. It's set to detect harmful intent. She told me she had no intention of harming anyone or anything while here." He slightly shifted his wing until Baijani started brushing again. "Except, potentially, our reputation. She snuck five messages into the box, and three of them were decoys. The decoys didn't get delivered, thankfully. They were all anonymous, generic, and intended to cause suspicion. But after I mentioned the star charts...</p>

<p>"She knew about the observatory. I figured...why not ask? We have a smattering of knowledge about Lycans, but that was...religious things. We didn't know anything, and the initial reports from the observatory were a mess."</p>

<p>"I'm surprised she told you anything, considering," Baijani said, shifting to his other side and starting to brush again. "The Lycans were always...touchy about their religion."</p>

<p>"And this especially so, going by the things she did say. But she said...she'd rather see someone using it, than let it crumble to dust. In exchange for that information, we took the two real letters she wanted to send."</p>

<p>"Well now I&rsquo;m curious."</p>

<p>"One for Hunger, and one for Litoria."</p>

<p>"Oh to see his face..."</p>

<p>Shadimon grinned, "I know. I left a note on that one to wait for a reply, just so someone might get to see him light his office on fire. But the fake letters...They were very specifically to people that I don't think she's encountered. Dust, Shyrendora, and Ehra, by name. She's a Lycan and a blade elf, so of course she knows who Ehra is. But Dust and Shyrendora? It didn't strike me as odd until after, but she seems surprisingly well-informed."</p>

<p>Baijani snorted in disgust as another feather came loose in her hand, "Important people. We knew about their leaders, why wouldn't they know about ours?"</p>

<p>"I suppose they did have spies too..." Shadimon muttered. "I wasn't...angry. I was shocked, indignant, curious, afraid, certainly, but not...angry. To see a werewolf in my village. She was even in hybrid form, and Hunger wasn't exaggerating how big she or that sword of hers are. I didn't hate her."</p>

<p>They sat in silence for a moment, broken only by the soft shushing sound of the brush against feathers. "That's what we were hoping for, wasn't it?" Baijani asked quietly.</p>

<p>"Hoping, yes, but it's one thing to say it. But...I just want the War to be over. I don't want to be what it turned me into, what it turned us into. But a werewolf, and specifically <i>this</i> werewolf that we know nearly killed Hunger <i>and Terror</i> for gods sakes. Terror damn near took Dreyrugr's head off, and this werewolf nearly got the better of her? So it's not like she's some civilian."</p>

<p>Baijani sighed hard, ruffling his short hair, "Brancher, she attacked Hunger. Do you really think he didn't provoke it somehow? And Terror came in swinging that monster hammer? That situation was doomed before it even started."</p>

<p>"I can't say you're wrong, but still. Going from provokation to violence isn't minor. She <i>bit</i> Hunger. He must be all right since he's still alive, but still."</p>

<p>"You were on guard of course, yes?"</p>

<p>"Yes of course."</p>

<p>"And you didn't have many options, being stuck indoors."</p>

<p>"No, a fight would have been suicide."</p>

<p>"So being civil wasn't an unreasonable reaction."</p>

<p>Shadimon looked past his shoulder at her, "You're humoring me."</p>

<p>"No son, I'm trying to help you. You feel guilty for not being angry at a person who you know tried to kill two of your allies. That's reasonable. You feel guilty because Lycans have been the Enemy for so long. You know in your head that the War is over, and you've been telling yourself that, but being confronted by a werewolf suddenly put that to the test in a way you weren't ready for yet."</p>

<p>She set aside the brush and started kneading the tight muscles across his shoulders, "It's the big question we're all dealing with now. Who are we, after the War? Who do we want to be? You just have it worse than most, since you're trying to figure that out for all of us as a people as well as yourself." She draped her arms over his shoulders, resting her chin on top of his head, "You need to take better care of yourself, Shadi."</p>

<p>Shadimon sighed and leaned back against her, "In what time? It feels like I'm busier than I was even at the height of the War. I get back here and stagger straight to bed most nights."</p>

<p>Baijani leaned over to peer at him, squinting, "I thought you'd lost weight. You're not eating enough. No wonder you're losing feathers. Shadi..." She sighed against his hair and arched her wings around him, "Shadi, people love you. All you have to do is say the word, and people will help you."</p>

<p>"I don't want people to worry," he murmured. "Or be afraid that...something is going wrong in the village."</p>

<p>"Shadi. No one here can keep a secret to save their lives. Anything going wrong is common knowledge. I'm honestly amazed that you've managed to keep this werewolf business quiet, especially since <i>Satri</i> of all people met her." He could hear the grin in her voice, "Disguised as a dreamdust elf, oh that was clever. No, people know things are going well. People complain about the issues with their work, but also brag about how well things are going. We're starting an entirely new settlement here; of course there's going to be turbulence. But you're flying so far ahead, no one can catch you if you fall.</p>

<p>"Now. What you're going to do is come home with me. We will feed you, and then you will get a proper night's rest in a family bed. And we will feed you again in the morning. Please, Shadi? Let us take care of you?"</p>

<p>He sighed, but it was fond, "I certainly can't turn down the pampering your family tends to throw at me."</p>

<p>"Very good. Come on, up with you."</p>

<p>Asavardi was warmly lit at night; each of the occupied homes had lanterns set outside and more light spilling through open doors and windows. The far side of the canyon glittered in the dark. Baijani's home was now framed by a wooden porch, set with small plant boxes and a hammock. The lattice windows were closed, but plenty of light came through, as well as the smells of cooking. "My children, we have another for dinner," Baijani called as she pushed the door open.</p>

<p>"Shadi!" Shrike, Baijani's daughter and his long-time friend, got up to give him a hug. "We were wondering when Ama was going to kidnap you again. Go on and make yourself at home. We're almost done here."</p>

<p>Shadimon let himself be shooed into the lounging nest, and before long the rest of the family joined him, setting out the large plates and bowls that were the usual for family-style meals. They all fussed over him - Baijani, her daughter, and granddaughters - and kept his plate full until they were satisfied. The last meal before bed was traditionally the largest for winged elves. They wouldn't even let him help with the cleanup after the meal, insisting he stay put and relax. Baijani leaned around the door from the bedroom, "You haven't been preening enough, have you at least been bathing?"</p>

<p>"I...yes?"</p>

<p>"I'm not encouraged. Go do that."</p>

<p>Shadimon sighed dramatically and pulled himself out of the sitting nest, "You aren't actually my mother, you know."</p>

<p>"No, but some days you need it."</p>

<p>"We all gave up arguing years ago, as you well know," Shrike said with a wink. "Let us know if you can't find anything in there."</p>

<p>While Shadimon was working cleaning powder into his feathers, one of Baijani's granddaughters came in and walked off with his clothes. "She won't even let me keep those?"</p>

<p>"Not until they've been washed, no!"</p>

<p><i>If I had a copper for every time I've sighed in the past year...</i></p>

<p>By the time he was finished, the lights in the main room had been dimmed, and the door and windows were latched. Baijani's family only consisted of six people for the time, so only one of the bedrooms was in use. Everyone had their preferred places, but Shadimon had joined them enough for everyone to adjust for him. Once everyone was settled, the room was warm and quiet, and the solidity of other people around him was more comforting than Shadimon had been willing to admit to.</p>

<p>Shadimon had slept alone for the past decade, at least primarily. Scout teams tended to pile when in the field, but that was as much safety as comfort. But none of his family had survived the War, and he had been too busy - and too hurt, when he could admit it - to make a new family. But on nights he stayed with Baijani's family, with someone's wing draped over his legs and someone else's arm across his chest, he felt the lack. They all accepted him as family, but they also knew it wasn't official. He suspected they were giving him space, but Baijani was right: being alone was doing him no favors. </p>

