<p>Naleryn sighed, "What we need is an expert, and that's exactly what we'll never get."</p>

<p>Anasatri paused, "What if we could?"</p>

<p>"What, in some impossible world where the Lycans weren't all gone? Sure, get someone who knows what this damn thing is and see if they could fix it!"</p>

<p>"Fix it, or explain it. And those monoliths that the surveyors mapped out."</p>

<p>"Sure, all of that. I wish the Loremaster hadn't died. An expert is really what we need here."</p>

<p>"I know an expert."</p>

<p>Naleryn stopped examining the gear in her hand and looked over at Anasatri, squinting. "Say again?"</p>

<p>Anasatri turned one of the planar models, "I know an expert. A werewolf. She's on her way here, because I asked for her help."</p>

<p>"...All right back up and start from the beginning." Naleryn put down the gear and came over to sit across from Anasatri.</p>

