    <h1>Mother's Nightmare</h1>
<p>Chaos on the landing field. Healers darted in and out like fish in a current. All of the Harriers and their scouts were streaked with blood and dust, their armor ragged and weapons broken. Aranan dove into it, lending a hand and a shoulder where it was needed, helping sort out the wounded and distribute water.</p>

<p>The crowd shifted and she found herself facing Shadimon. She rushed to him, clasping his shoulders and looking him over. He didn't appear to be hurt, but she didn't like the glassiness in his eyes. "Shadi, thank the Mother. What happened?"</p>

<p>"Ambush," he said faintly. "Took out the ground pickets and were on us before we could get up." He looked up at her, forcing himself to focus through the shock and exhaustion. "Ara...Kiren and Anjal..."</p>

<p>The ground dropped away from under Aranan, leaving Shadimon's next words to echo into the endless nothing that suddenly roared over her.</p>

<p>"They fell..."</p>

<p>The words meant nothing. She had seen them both only a few hours before. They couldn't be gone. A few hours weren't enough for the entire world to shift.</p>

<p>"Ara..."</p>

<p>She didn't know where she was going. She had to move. She had to...what? There were voices around her, but they didn't matter. Nothing mattered. Nothing could ever matter again.</p>

<p><i>They fell.</i></p>

<p>No, the words meant nothing. Nothing, just emptiness and a building scream. It was growing, under her keel with enough force that it felt strong enough to lift her. A storm of grief and shock and fury that threatened to throw her into the sky and bring her crashing back down. It clawed its way up her throat and tore through her in a furious, hopeless scream.</p>

<p><i>They fell.</i></p>

<p>Her children, her bright-eyed, broad-winged twins.</p>

<p><i>They fell.</i></p>

<p>Grief and fury, impossible to distinguish, a snarling, raging storm that demanded action.</p>

<p><i><b>They fell.</b></i></p>

<p>Something hit her, or she ran into something. It didn't matter, she had to move. She had to go, she had to find them. Aranan no longer mattered; all that mattered was the rage burning through her body, and the need to do something, anything, to find a target, to make someone else hurt as much as she did.</p>

<p>"Ara! Aranan!"</p>

<p>She knew that voice. Who? Not a target...</p>

<p>"Ara, stay with me."</p>

<p>Someone was holding onto her. Someone she trusted, that voice...</p>

<p>She couldn't see. It didn&rsquo;t matter. She didn't need to. All she needed...</p>

<p>Hands cupped her jaw, forcing her to look down. Her vision cleared enough to see familiar gold eyes, filled with grief and tears, just like hers...</p>

<p>Something in her broke and she screamed. Arms pulled her close while her mother's voice whispered, an anchor in the maelstrom.</p>

<p><i>They fell...</i></p>

<p>~~</p>

<p>Shrike jerked awake, gasping in the darkness. Someone else shifted, "Aunt?"</p>

<p>She ran a hand over her face, "Just a nightmare. Just...a bad one."</p>

<p>Chandra shifted, fumbling in the dark until her hand found Shrike's arm, "Need anything?"</p>

<p>"Some air, I think. Go back to sleep; I'll be fine."</p>

<p>"Mh, all right. Hope you can get back to sleep."</p>

<p>Shrike carefully extricated herself from the bed, padding into the den. The shutters were closed and there were still coals in the stove, but it was cold, especially coming directly from bed. Some icy cold water splashed on her face did an admirable job of waking her up, but the dream clung, sending shudders down her spine. She paced across the room a few times; she had to move, but at night and in the cold was no time to get out and fly. Finally, she ducked back into the bedroom long enough to grab a cloak, tied it around herself, and went out the front door.</p>

<p>~~</p>

<p>Shadimon jolted awake when someone banged on his door. He sat up, waiting and listening, <i>Did I really hear that or was I dreaming?</i></p>

<p>The banging came again, a bit less insistent. Slowly standing up, he grabbed a javelin from the racked quiver and made his way into the dim living room. He paused at the door, waiting until the knocking came again.</p>

<p>"Mother's breath it's the backside of midnight, who is it?"</p>

<p>"Shrike." There was a pause, "Can I come in Shadi, please?"</p>

<p>He peered through the small window panel set in the door, seeing just one silhouette outside, framed by dim starlight and pinpoints of lanterns from the opposite cliff. He eased the door open, javelin still at the ready, but it really was Shrike and she was alone. Shadimon squinted in the dark, "Come in...What...are you wearing anything under that cloak? Get in here before you freeze!"</p>

<p>By the time Shrike latched the door behind herself, Shadimon had gotten a blanket from the nest and tossed it to her before going to stoke up the coals in the stove. Shrike stood in the entry, clutching the blanket around herself. "Goddess, Shrike, did you fly over here?"</p>

<p>"There was enough light," she muttered.</p>

<p>Shadimon lit a lamp and looked at her, taking in the ruffled feathers and distant expression. "Nightmares?"</p>

<p>Her eyes snapped back to him, "Yes. About the day...when you had to tell me..."</p>

<p>Shadimon winced and went to her, running his hands up her arms to her shoulders, "I'm sorry, Ara..."</p>

<p>"So am I." She looked around, as if just realizing where she was. "I just...I need to be...somewhere else. I can't sleep more tonight. I need to do something. I'm sorry to wake you up, I don't...I just..." She shuddered as Shadimon put his arms around her, leaning her head against his. "Can I stay here?" she asked quietly.</p>

<p>"Of course, you know you don't have to ask."</p>

<p>"No, I mean...long term. Permanently."</p>

<p>He pulled back to study her face, "You...want to household?"</p>

<p>"Yes."</p>

<p>"I'm definitely not opposed, but we should...probably talk about this when we're both properly awake and put together."</p>

<p>"There's time." Shrike sharply pulled him back to her, brushing her cheek against his and dragging her teeth against his ear, "But for now, please...?"</p>

<p>Shadimon sighed and chuckled, "Fine. I owe you anyway."</p>

<p>Sometime later - much later, Shadimon noticed with some fond resignation - Shrike fell asleep across his chest in a messy tangle of blankets.</p>

<p>~~</p>

<p>Baijani wasn't surprised to find Shrike gone in the morning. It wouldn't be the first time she'd had nightmares and needed to get up in the middle of the night. The messy note by the door saying she was going to stay elsewhere wasn't entirely expected, but not terribly surprising. However, Baijani was surprised when Shrike walked into her office mid-afternoon, carrying a pack.</p>

<p>"Shadi and I talked; we're going to household."</p>

<p>Baijani stared for a moment before throwing up her hands, scattering papers, "Thank the Mother! You can learn!"</p>

<p>Shrike blinked before laughing too, "You've been waiting for that for a long time haven't you?"</p>

<p>"You have no idea. I'm so happy to hear it. So you're going to move over there..." she trailed off for a moment. "Oh, oh my." She covered her mouth, trying not to laugh again.</p>

<p>"What? Have you been scheming again?"</p>

<p>"You know Tathariel has been helping Shadi around the house? Well, he was in here not three days ago asking for advice. Apparently he's <i>quite</i> smitten with Shadi."</p>

<p>Shrike doubled over laughing, "Oh, oh this is perfect. So he's looking for something romantic?"She rubbed her hands together, "Good, I can make sure Shadi doesn't run confused once he realizes what's going on." She sat down on the edge of her mother's desk, "I'm so happy he isn't running anymore, but I don't want to push him too fast. Me moving in with him will help, but one relationship isn't enough."</p>

<p>Baijani very casually moved some papers around, "Tathariel has a sister, if you didn't know. Vatsarava. I think the lot of you would get along quite well."</p>

<p>Shrike smothered another laugh, "Ama you never stop, do you."</p>

<p>"I'd get bored. I agree that pushing too fast isn't a good idea, but you probably will want to talk to Tathariel. Let him know you&rsquo;re on his side, at least."</p>

